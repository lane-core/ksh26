emit_ninja()
{
	configure_log "build.ninja"
	_ninja="$BUILDDIR/build.ninja"

	# Collect sources
	_libast_srcs=$(collect_libast_sources)
	_libcmd_srcs=$(collect_libcmd_sources)
	_ksh26_srcs=$(collect_ksh26_sources)

	# Include paths for each library
	# libast: include every subdirectory (except features/ and man/)
	# so internal headers (cdtlib.h, optlib.h, strtoi.h, etc.) resolve
	_ast_sub_incs=""
	for _d in "$LIBAST_SRC"/*/; do
		case $_d in
		*/features/|*/man/)	continue ;;
		esac
		_ast_sub_incs="$_ast_sub_incs -I$_d"
	done
	# std/ wrappers (stdio.h → ast_stdio.h) are in $FEATDIR/libast/std/,
	# kept separate from $FEATDIR/libast/ so iffe probes don't pick them up.
	_std_inc="-I$FEATDIR/libast/std"
	_ast_incs="$_std_inc -I$FEATDIR/libast $_ast_sub_incs -I$LIBAST_SRC"
	_cmd_incs="$_std_inc -I$FEATDIR/libcmd -I$LIBCMD_SRC -I$FEATDIR/libast -I$LIBAST_SRC/include"
	_ksh_incs="$_std_inc -I$FEATDIR/ksh26 -I$KSH_SRC -I$KSH_SRC/include -I$FEATDIR/libast -I$LIBAST_SRC/include -I$LIBCMD_SRC"
	_pty_incs="$_std_inc -I$FEATDIR/pty -I$PTY_SRC -I$LIBCMD_SRC -I$FEATDIR/libast -I$LIBAST_SRC/include"

	{
		# Preservation 9: Self-documenting header
		cat <<HEADER
# build.ninja — generated by configure.sh
# HOSTTYPE: $HOSTTYPE
# CC: $(command -v "$CC" 2>/dev/null || echo "$CC")
# Date: $(date -u '+%Y-%m-%dT%H:%M:%SZ' 2>/dev/null || date)
# Do not edit — regenerate with: ./configure.sh

ninja_required_version = 1.3

HEADER

		# Variables
		cat <<VARS
cc = $CC
cflags = $CFLAGS_BASE
ldflags = $LDFLAGS_BASE
libs = $LIBS

# Per-library flags
ast_cflags = -D_BLD_ast -DHOSTTYPE='"$HOSTTYPE"' \$cflags $_ast_incs
cmd_cflags = -D_BLD_cmd -DERROR_CATALOG='"libcmd"' -DHOSTTYPE='"$HOSTTYPE"' \$cflags $_cmd_incs
ksh_cflags = -D_BLD_ksh -DSH_DICT='"libshell"' -D_API_ast=20100309 \$cflags $_ksh_incs
pty_cflags = -DERROR_CATALOG='"builtin"' \$cflags $_pty_incs

VARS

		# Rules
		cat <<'RULES'
rule cc_ast
  command = $cc $ast_cflags -MD -MF $out.d -c $in -o $out
  depfile = $out.d
  deps = gcc
  description = CC [ast] $out

rule cc_cmd
  command = $cc $cmd_cflags -MD -MF $out.d -c $in -o $out
  depfile = $out.d
  deps = gcc
  description = CC [cmd] $out

rule cc_ksh
  command = $cc $ksh_cflags $extra_cflags -MD -MF $out.d -c $in -o $out
  depfile = $out.d
  deps = gcc
  description = CC [ksh] $out

rule cc_pty
  command = $cc $pty_cflags -MD -MF $out.d -c $in -o $out
  depfile = $out.d
  deps = gcc
  description = CC [pty] $out

rule ar
  command = rm -f $out && ar rcs $out $in
  description = AR $out

rule link
  command = $cc $ldflags -o $out $in $libs
  description = LINK $out

RULES

		# ── libast objects ──────────────────────────────────
		_ast_objs=""
		for _src in $_libast_srcs; do
			_base=$(basename "$_src" .c)
			_obj="$OBJDIR/libast/${_base}.o"
			_ast_objs="$_ast_objs $_obj"
			printf 'build %s: cc_ast %s\n' "$_obj" "$_src"
		done
		echo

		# libast.a
		printf 'build %s/libast.a: ar%s\n\n' "$LIBDIR" "$_ast_objs"

		# ── libcmd objects ──────────────────────────────────
		_cmd_objs=""
		for _src in $_libcmd_srcs; do
			_base=$(basename "$_src" .c)
			_obj="$OBJDIR/libcmd/${_base}.o"
			_cmd_objs="$_cmd_objs $_obj"
			printf 'build %s: cc_cmd %s\n' "$_obj" "$_src"
		done
		echo

		# libcmd.a
		printf 'build %s/libcmd.a: ar%s\n\n' "$LIBDIR" "$_cmd_objs"

		# ── ksh26 objects (libshell) ────────────────────────
		_ksh_objs=""
		for _src in $_ksh26_srcs; do
			_base=$(basename "$_src" .c)
			_obj="$OBJDIR/ksh26/${_base}.o"
			_ksh_objs="$_ksh_objs $_obj"
			# SHOPT_DYNAMIC=0 only needed for files that reference it
			_extra=""
			case "$_src" in
			*/data/builtins.c|*/bltins/typeset.c|*/sh/path.c)
				_extra="-DSHOPT_DYNAMIC=0" ;;
			esac
			printf 'build %s: cc_ksh %s\n' "$_obj" "$_src"
			if [ -n "$_extra" ]; then
				printf '  extra_cflags = %s\n' "$_extra"
			fi
		done
		echo

		# libshell.a (all ksh objects except pmain.o)
		_shell_objs=""
		for _o in $_ksh_objs; do
			case "$_o" in
			*/pmain.o)	;;
			*/shcomp.o)	;;
			*)		_shell_objs="$_shell_objs $_o" ;;
			esac
		done
		printf 'build %s/libshell.a: ar%s\n\n' "$LIBDIR" "$_shell_objs"

		# ── Link libraries ──────────────────────────────────
		_link_libs="$LIBDIR/libshell.a $LIBDIR/libast.a $LIBDIR/libcmd.a $LIBDIR/libast.a"

		# ── ksh binary ──────────────────────────────────────
		printf 'build %s/ksh: link %s/ksh26/pmain.o %s\n' "$BINDIR" "$OBJDIR" "$_link_libs"
		printf '  libs = %s\n\n' "$LIBS"

		# ── shcomp binary ───────────────────────────────────
		printf 'build %s/shcomp: link %s/ksh26/shcomp.o %s\n' "$BINDIR" "$OBJDIR" "$_link_libs"
		printf '  libs = %s\n\n' "$LIBS"

		# ── pty binary (for tests) ──────────────────────────
		printf 'build %s/pty.o: cc_pty %s/pty.c\n' "$OBJDIR" "$PTY_SRC"
		if test -f "$PTY_SRC/pty_main.c"; then
			printf 'build %s/pty_main.o: cc_pty %s/pty_main.c\n' "$OBJDIR" "$PTY_SRC"
			printf 'build %s/pty: link %s/pty.o %s/pty_main.o %s/libast.a %s/libcmd.a %s/libast.a\n' \
				"$BINDIR" "$OBJDIR" "$OBJDIR" "$LIBDIR" "$LIBDIR" "$LIBDIR"
		else
			printf 'build %s/pty: link %s/pty.o %s/libast.a %s/libcmd.a %s/libast.a\n' \
				"$BINDIR" "$OBJDIR" "$LIBDIR" "$LIBDIR" "$LIBDIR"
		fi
		printf '  libs = %s\n\n' "$LIBS"

		# ── Default target ──────────────────────────────────
		printf 'default %s/ksh %s/shcomp %s/pty\n' "$BINDIR" "$BINDIR" "$BINDIR"

		# ── Test targets ────────────────────────────────────
		# samu test                        — run all tests
		# samu test/basic.C.stamp          — run one test
		# samu -k 0 test                   — run all, continue on failure

		_test_runner="$BINDIR/run-test"

		cat <<NINJA

# ── Tests ──────────────────────────────────────────────────

rule test
  command = sh $_test_runner \$in \$mode \$out
  description = TEST \$desc

rule test_serial
  command = sh $_test_runner \$in \$mode \$out
  description = TEST \$desc
  pool = serial

pool serial
  depth = 1

NINJA

		_all_stamps=""
		# Serial pool: reduces scheduling jitter for tests with timing deps.
		# Distinct from advisory (flake.nix): serial = execution strategy,
		# advisory = gate policy. A test can be serial but still gate.
		_timesensitive="builtins io options sigchld signal subshell"

		for _test_sh in "$KSH_SRC/tests"/*.sh; do
			[ -f "$_test_sh" ] || continue
			_tname=${_test_sh##*/}
			_tname=${_tname%.sh}
			# Skip internal files
			case "$_tname" in _*) continue ;; esac

			# Time-sensitive tests run serially to reduce scheduling jitter
			_rule="test"
			case " $_timesensitive " in
			*" $_tname "*) _rule="test_serial" ;;
			esac

			# Extra build deps for specific tests
			_extra_deps=""
			case "$_tname" in
			pty) _extra_deps=" $BINDIR/pty" ;;
			esac

			# C locale variant
			printf '\nbuild test/%s.C.stamp: %s %s | %s/ksh %s/shcomp%s %s %s/test-env.sh\n' \
				"$_tname" "$_rule" "$_test_sh" "$BINDIR" "$BINDIR" "$_extra_deps" "$_test_runner" "$BUILDDIR"
			printf '  mode = C\n  desc = %s (C)\n' "$_tname"
			_all_stamps="$_all_stamps test/${_tname}.C.stamp"

			# C.UTF-8 locale variant
			printf '\nbuild test/%s.C.UTF-8.stamp: %s %s | %s/ksh %s/shcomp%s %s %s/test-env.sh\n' \
				"$_tname" "$_rule" "$_test_sh" "$BINDIR" "$BINDIR" "$_extra_deps" "$_test_runner" "$BUILDDIR"
			printf '  mode = C.UTF-8\n  desc = %s (C.UTF-8)\n' "$_tname"
			_all_stamps="$_all_stamps test/${_tname}.C.UTF-8.stamp"
		done

		# Phony target to run all tests
		printf '\nbuild test: phony%s\n' "$_all_stamps"

	} | atomic_write "$_ninja" && configure_log "build.ninja ... updated" \
	  || configure_log "build.ninja ... unchanged"
}

# emit/headers.sh — generate derived headers (git.h, shopt.h, cmdext.h, ast_release.h)
# Called during Phase 2 (generators).
# Depends: core.sh (PACKAGEROOT, FEATDIR, KSH_SRC, LIBCMD_SRC, BUILDDIR)

generate_ast_release_h()
{
	LOCAL _rel _git_branch; BEGIN
		_rel="$FEATDIR/libast/ast_release.h"
		{
			_git_branch=$(git -C "$PACKAGEROOT" branch --show-current 2>/dev/null) || _git_branch=""
			putln '/* generated by configure.sh */'
			case $_git_branch in
			[0-9]*.[0-9]*)
				if git -C "$PACKAGEROOT" diff-index --quiet HEAD 2>/dev/null; then
					putln '#ifndef _AST_release'
					putln '#    define _AST_release	1'
					putln '#endif'
				else
					putln '/* on release branch, but changes made */'
				fi
				;;
			"")
				putln '#ifndef _AST_release'
				putln '#    define _AST_release	1'
				putln '#endif'
				;;
			*)
				putln '/* not on a release branch */'
				;;
			esac
		} | atomic_write "$_rel" || true
	END
}

generate_git_h()
{
	LOCAL _git_h _commit; BEGIN
		_git_h="$FEATDIR/ksh26/git.h"
		_commit=$(git -C "$PACKAGEROOT" rev-parse --short=8 HEAD 2>/dev/null) || _commit=""
		if not str empty "$_commit"; then
			git -C "$PACKAGEROOT" update-index --really-refresh >/dev/null 2>&1 || true
			if ! git -C "$PACKAGEROOT" diff-index --quiet HEAD 2>/dev/null; then
				_commit="${_commit}/MOD"
			fi
			printf '/* generated by configure.sh */\n#define git_commit "%s"\n' "$_commit" \
				| atomic_write "$_git_h" || true
		else
			printf '/* generated by configure.sh (not in a git repo) */\n#undef git_commit\n' \
				| atomic_write "$_git_h" || true
		fi
	END
}

generate_shopt_h()
{
	LOCAL _shopt_h; BEGIN
		_shopt_h="$FEATDIR/ksh26/shopt.h"
		{
			putln '/* Generated from ksh26/SHOPT.sh by configure.sh */'
			putln ""

			SHOPT()
			{
				case "$1" in
				*=*)
					_sname="SHOPT_${1%%=*}"
					_sval="${1#*=}"
					;;
				*)	return ;;
				esac

				case "$_sname" in
				SHOPT_DEVFD)
					if test -d /dev/fd; then _sval=1; else return; fi ;;
				SHOPT_MULTIBYTE)
					if str empty "$_sval"; then _sval=1; fi ;;
				SHOPT_TEST_L|SHOPT_PRINTF_LEGACY)
					if str empty "$_sval"; then return; fi ;;
				*)
					if str empty "$_sval"; then return; fi ;;
				esac

				case "$_sval" in
				[0-9]*)
					printf '#ifndef %s\n#   define %s\t%s\n#endif\n\n' \
						"$_sname" "$_sname" "$_sval" ;;
				\"*\")
					printf '#ifndef %s\n#   define %s\t%s\n#endif\n\n' \
						"$_sname" "$_sname" "$_sval" ;;
				*)
					printf '#ifndef %s\n#   define %s\t"%s"\n#endif\n\n' \
						"$_sname" "$_sname" "$_sval" ;;
				esac
			}
			. "$KSH_SRC/SHOPT.sh"

			putln '#include "FEATURE/options"'
			putln ""
			putln '/* overrides */'
			putln '#if SHOPT_SCRIPTONLY'
			putln '#   undef SHOPT_ACCT'
			putln '#   undef SHOPT_AUDIT'
			putln '#   undef SHOPT_ESH'
			putln '#   undef SHOPT_HISTEXPAND'
			putln '#   undef SHOPT_SYSRC'
			putln '#   undef SHOPT_VSH'
			putln '#endif'
			putln '#if !_sys_acct'
			putln '#   undef SHOPT_ACCT'
			putln '#endif'
		} | atomic_write "$_shopt_h" || true
	END
}

generate_cmd_headers()
{
	LOCAL _cmd_dir; BEGIN
		_cmd_dir="$BUILDDIR/libcmd_gen"
		mkdir -p "$_cmd_dir"

		{
			printf '/*\n * -lcmd extern function prototypes\n */\n\n'
			sed -e '/^b_[a-z_][a-z_0-9]*(/!d' \
				-e 's/^b_//' \
				-e 's/(.*//' \
				-e 's/.*/extern int	b_&(int, char**, Shbltin_t*);/' \
				"$LIBCMD_SRC"/*.c | sort -u
		} | atomic_write "$_cmd_dir/cmdext.h" || true
		cp -f "$_cmd_dir/cmdext.h" "$FEATDIR/libast/cmdext.h"

		{
			printf '/*\n * -lcmd function list -- define your own CMDLIST()\n */\n\n'
			sed -e '/^b_[a-z_][a-z_0-9]*(/!d' \
				-e 's/^b_//' \
				-e 's/(.*//' \
				-e 's/.*/CMDLIST(&)/' \
				"$LIBCMD_SRC"/*.c | sort -u
		} | atomic_write "$_cmd_dir/cmdlist.h" || true
		cp -f "$_cmd_dir/cmdlist.h" "$FEATDIR/libast/cmdlist.h"
	END
}

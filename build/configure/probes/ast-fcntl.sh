# probe: ast-fcntl — .c file probe for fcntl features
# Tier 3 (delegate). Compiles src/lib/libast/features/fcntl.c in a
# workdir with FEATURE symlink, captures output.
#
# Lifted from monolith probe_ast_fcntl with API translations:
# - output path from $1
# - stderr via probe_run (routed by _PROBE_STDERR)

probe_ast_fcntl()
{
	_out="$1"

	# Cache check
	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/fcntl.c" ]; then
		return 0
	fi

	# fcntl.c uses #include "FEATURE/..." which resolves relative to the
	# source file. Copy to a workdir that has a FEATURE symlink.
	_fcntl_work="$FEATDIR/libast/_work_fcntl"
	mkdir -p "$_fcntl_work"
	[ -L "$_fcntl_work/FEATURE" ] || ln -sf "$FEATDIR/libast/FEATURE" "$_fcntl_work/FEATURE"
	cp "$LIBAST_SRC/features/fcntl.c" "$_fcntl_work/fcntl.c"

	# No -I.../std — that has the ast stdio.h wrapper which remaps printf to _ast_printf
	_fcntl_inc="-I$FEATDIR/libast -I$LIBAST_SRC -I$LIBAST_SRC/comp -I$LIBAST_SRC/include"
	_fcntl_bin="$_fcntl_work/fcntl_probe"

	# fcntl.c uses printf but has no #include <stdio.h>. iffe's preamble
	# provided it. Include the standards header FIRST so _GNU_SOURCE is
	# defined before glibc's <features.h> processes (via stdio.h).
	# Without this, glibc hides O_DIRECTORY behind __USE_GNU.
	_fcntl_content=""
	if probe_run "$CC" $CFLAGS_BASE $_fcntl_inc \
		-include "$FEATDIR/libast/FEATURE/standards" -include stdio.h \
		-o "$_fcntl_bin" "$_fcntl_work/fcntl.c" $LDFLAGS_BASE; then
		_fcntl_content=$(probe_run "$_fcntl_bin") || true
	else
		echo "configure.sh: warning: fcntl.c probe failed to compile" >&2
	fi
	rm -f "$_fcntl_bin"

	{
		echo "/* : : generated by configure.sh probe_ast_fcntl : : */"
		echo "#ifndef _def_fcntl_ast"
		echo "#define _def_fcntl_ast	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s\n' "$_fcntl_content"
		echo "#endif"
	} | atomic_write "$_out" || true
}

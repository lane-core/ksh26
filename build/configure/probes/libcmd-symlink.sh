# probe: libcmd-symlink — symlink features
# Tier 7. Detects lchmod/lchown symlink features.
# Uses execute{} probes to verify the functions actually work,
# not just that they link.

probe_libcmd_symlink()
{
	_out="$1"
	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBCMD_SRC/features/symlink" ]; then
		return 0
	fi

	_defs=""

	# lib lchmod execute{} — check that lchmod actually works
	if _mc_execute <<'EOF'
#include <sys/types.h>
#include <sys/stat.h>
#include <errno.h>
int
main(void)
{
	lchmod("No-FiLe", 0);
	return errno != ENOENT;
}
EOF
	then
		_defs="${_defs}#define _lib_lchmod	1	/* lchmod implemented */
"
	fi

	# lib lchown execute{} — check that lchown actually works
	if _mc_execute <<'EOF'
#include <sys/types.h>
#include <sys/stat.h>
#include <errno.h>
int
main(void)
{
	lchown("No-FiLe", 0, 0);
	return errno != ENOENT;
}
EOF
	then
		_defs="${_defs}#define _lib_lchown	1	/* lchown implemented */
"
	fi

	{
		echo "/* : : generated by configure.sh probe_libcmd_symlink : : */"
		echo "#ifndef _def_symlink_cmd"
		echo "#define _def_symlink_cmd	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s' "$_defs"
		echo "#endif"
	} | atomic_write "$_out" || true
}

# probe: ast-mode — .c file probe for mode (file permission) features
# Tier 5 (delegate). Compiles src/lib/libast/features/mode.c in a
# workdir with FEATURE symlink, captures output.
#
# Lifted from monolith probe_ast_mode with API translations:
# - output path from $1

probe_ast_mode()
{
	_out="$1"

	# Cache check
	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/mode.c" ]; then
		return 0
	fi

	# .c program probe — needs FEATURE symlink + no std/
	_mode_work="$FEATDIR/libast/_work_mode"
	mkdir -p "$_mode_work"
	[ -L "$_mode_work/FEATURE" ] || ln -sf "$FEATDIR/libast/FEATURE" "$_mode_work/FEATURE"
	cp "$LIBAST_SRC/features/mode.c" "$_mode_work/mode.c"

	# No -I.../std — ast stdio.h wrapper remaps printf
	# mode.c needs modecanon.h from include/
	_mode_inc="-I$FEATDIR/libast -I$LIBAST_SRC -I$LIBAST_SRC/comp -I$LIBAST_SRC/include"
	_mode_bin="$_mode_work/mode_probe"

	_mode_content=""
	if "$CC" $CFLAGS_BASE $_mode_inc -include stdio.h \
		-o "$_mode_bin" "$_mode_work/mode.c" $LDFLAGS_BASE 2>/dev/null; then
		_mode_content=$("$_mode_bin" 2>/dev/null) || true
	else
		echo "configure.sh: warning: mode.c probe failed to compile" >&2
	fi
	rm -f "$_mode_bin"

	{
		echo "/* : : generated by configure.sh probe_ast_mode : : */"
		echo "#ifndef _def_mode_ast"
		echo "#define _def_mode_ast	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s\n' "$_mode_content"
		echo "#endif"
	} | atomic_write "$_out" || true
}

# probe: ksh26-cmds — ed path detection
# Tier 7. Replaces: run_iffe "cmds" for cmd universe, pth ed.
# Pure shell logic, no C compilation.

probe_ksh26_cmds()
{
	_out="$1"
	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$KSH_SRC/features/cmds" ]; then
		return 0
	fi

	_defs=""

	# cmd universe — always false on modern systems
	# (universe(3) was a Pyramid/Sequent thing)

	# pth ed — find ed on PATH
	_ed_path=""
	for _p in /bin/ed /usr/bin/ed; do
		if [ -x "$_p" ]; then
			_ed_path="$_p"
			break
		fi
	done
	if [ -n "$_ed_path" ]; then
		_defs="${_defs}#define _pth_ed	\"${_ed_path}\"	/* ed path */
"
	else
		# fail{} block: define with fallback
		_defs="${_defs}#define _pth_ed	\"ed\" /* ed not found on standard PATH */
"
	fi

	{
		echo "/* : : generated by configure.sh probe_ksh26_cmds : : */"
		echo "#ifndef _def_cmds_ksh26"
		echo "#define _def_cmds_ksh26	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s' "$_defs"
		echo "#endif"
	} | atomic_write "$_out" || true
}

# probe: ast-sys — system capabilities (headers, types, extern prototypes)
# Tier 2. Tests standard headers, socklen_t, max_align_t, and
# generates extern prototypes for unprototyped functions.

probe_ast_sys()
{
	_out="$1"

	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/sys" ]; then
		return 0
	fi

	_sys_inc="-I$FEATDIR/libast -I$LIBAST_SRC -I$LIBAST_SRC/comp -I$LIBAST_SRC/include"
	_saved_CFLAGS_BASE="$CFLAGS_BASE"
	CFLAGS_BASE="$CFLAGS_BASE $_sys_inc"

	_sys_headers=""
	_sys_defs=""
	_sys_externs=""

	# header directives — test each, emit #include if found
	for _h in stdlib.h stddef.h sys/types.h stdint.h inttypes.h \
		  string.h unistd.h limits.h fcntl.h locale.h; do
		case $_h in
		sys/*)	_test_ok=0
			_sname=${_h#sys/}
			_sname=${_sname%.h}
			if _mc_sys "$_sname"; then
				_test_ok=1
			fi
			;;
		*)	_test_ok=0
			if _mc_hdr "$_h"; then
				_test_ok=1
			fi
			;;
		esac
		if [ "$_test_ok" = 1 ]; then
			_sys_headers="${_sys_headers}#include <${_h}>
"
		fi
	done

	# typ socklen_t sys/socket.h — tests time.h,sys/time.h,sys/times.h
	# implicitly, then sys/socket.h, then the type
	_typ_implicit_defs=""
	if _mc_hdr "time.h"; then
		_typ_implicit_defs="${_typ_implicit_defs}#define _hdr_time	1	/* #include <time.h> ok */
"
	fi
	if _mc_sys "time"; then
		_typ_implicit_defs="${_typ_implicit_defs}#define _sys_time	1	/* #include <sys/time.h> ok */
"
	fi
	if _mc_sys "times"; then
		_typ_implicit_defs="${_typ_implicit_defs}#define _sys_times	1	/* #include <sys/times.h> ok */
"
	fi
	_has_sys_socket=0
	if _mc_sys "socket"; then
		_has_sys_socket=1
		_typ_implicit_defs="${_typ_implicit_defs}#define _sys_socket	1	/* #include <sys/socket.h> ok */
"
	fi

	# typ socklen_t test
	_typ_socklen=0
	if [ "$_has_sys_socket" = 1 ] && _mc_typ socklen_t sys/socket.h; then
		_typ_socklen=1
		_typ_implicit_defs="${_typ_implicit_defs}#define _typ_socklen_t	1	/* socklen_t is a type */
"
	fi

	# typ max_align_t stddef.h
	if _mc_typ max_align_t stddef.h; then
		_typ_implicit_defs="${_typ_implicit_defs}#define _typ_max_align_t	1	/* max_align_t is a type */
"
	fi

	# extern directives — test if function needs a prototype (npt test).
	# iffe's $usr accumulates all header directives' includes.
	# By the time extern runs, all 10 header includes are in scope,
	# plus sys/socket.h from the typ test.
	_npt_preamble="${_PROBE_STD_INC}
#include <stdlib.h>
#include <stddef.h>
#include <sys/types.h>
#include <stdint.h>
#include <inttypes.h>
#include <string.h>
#include <unistd.h>
#include <limits.h>
#include <fcntl.h>
#include <locale.h>
#include <sys/socket.h>"

	# Test each extern: compile bogus struct prototype; if it compiles,
	# the function is NOT prototyped → emit real prototype
	_sys_npt_eaccess=0
	if _mc_compile <<EOF
$_npt_preamble
struct _iffe_struct { int _iffe_member; };
extern struct _iffe_struct* eaccess (struct _iffe_struct*);
EOF
	then _sys_npt_eaccess=1; fi

	_sys_npt_memalign=0
	if _mc_compile <<EOF
$_npt_preamble
struct _iffe_struct { int _iffe_member; };
extern struct _iffe_struct* memalign (struct _iffe_struct*);
EOF
	then _sys_npt_memalign=1; fi

	# fsync, ftruncate, strlcat, strlcpy — also tested but usually
	# prototyped on modern systems
	_sys_npt_fsync=0
	if _mc_compile <<EOF
$_npt_preamble
struct _iffe_struct { int _iffe_member; };
extern struct _iffe_struct* fsync (struct _iffe_struct*);
EOF
	then _sys_npt_fsync=1; fi

	_sys_npt_ftruncate=0
	if _mc_compile <<EOF
$_npt_preamble
struct _iffe_struct { int _iffe_member; };
extern struct _iffe_struct* ftruncate (struct _iffe_struct*);
EOF
	then _sys_npt_ftruncate=1; fi

	_sys_npt_strlcat=0
	if _mc_compile <<EOF
$_npt_preamble
struct _iffe_struct { int _iffe_member; };
extern struct _iffe_struct* strlcat (struct _iffe_struct*);
EOF
	then _sys_npt_strlcat=1; fi

	_sys_npt_strlcpy=0
	if _mc_compile <<EOF
$_npt_preamble
struct _iffe_struct { int _iffe_member; };
extern struct _iffe_struct* strlcpy (struct _iffe_struct*);
EOF
	then _sys_npt_strlcpy=1; fi

	# Build extern output
	[ "$_sys_npt_eaccess" = 1 ] && _sys_externs="${_sys_externs}extern int	eaccess(const char*, int);
"
	[ "$_sys_npt_fsync" = 1 ] && _sys_externs="${_sys_externs}extern int	fsync(int);
"
	[ "$_sys_npt_ftruncate" = 1 ] && _sys_externs="${_sys_externs}extern int	ftruncate(int, off_t);
"
	[ "$_sys_npt_memalign" = 1 ] && _sys_externs="${_sys_externs}extern void*	memalign(size_t, size_t);
"
	[ "$_sys_npt_strlcat" = 1 ] && _sys_externs="${_sys_externs}extern size_t	strlcat(char*, const char*, size_t);
"
	[ "$_sys_npt_strlcpy" = 1 ] && _sys_externs="${_sys_externs}extern size_t	strlcpy(char*, const char*, size_t);
"

	CFLAGS_BASE="$_saved_CFLAGS_BASE"

	{
		echo "/* : : generated by configure.sh probe_ast_sys : : */"
		echo "#ifndef _AST_SYS_H"
		echo "#define _AST_SYS_H	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s' "$_sys_headers"
		printf '%s' "$_typ_implicit_defs"
		printf '%s' "$_sys_externs"
		echo "#include <stdarg.h>"
		echo "#endif"
	} | atomic_write "$_out" || true
}

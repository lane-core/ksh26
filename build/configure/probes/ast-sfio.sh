# probe: ast-sfio — sfio type sizing
# Tier 2. Tests 'signed' keyword, pointer bit width,
# void*>int comparison, long>int comparison.

probe_ast_sfio()
{
	_out="$1"

	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/sfio" ]; then
		return 0
	fi

	_sfio_inc="-I$FEATDIR/libast -I$LIBAST_SRC -I$LIBAST_SRC/comp -I$LIBAST_SRC/include"
	_saved_CFLAGS_BASE="$CFLAGS_BASE"
	CFLAGS_BASE="$CFLAGS_BASE $_sfio_inc"

	_sfio_defs=""

	# key signed — test if 'signed' is a reserved keyword (always true in C23)
	if _mc_compile <<'EOF'
signed int x = 0;
EOF
	then
		_sfio_defs="${_sfio_defs}#define _key_signed	1	/* signed is a reserved keyword */
"
	fi

	# _ptr_bits — number of bits in pointer
	_ptr_bits=$(_mc_output <<'EOF'
#include <stdio.h>
int
main(void)
{
	printf("#define _ptr_bits	%d\n", (int)(sizeof(char*) * 8));
	return 0;
}
EOF
)
	[ -n "$_ptr_bits" ] && _sfio_defs="${_sfio_defs}${_ptr_bits}
"

	# more void_int — voidptr is larger than int
	if _mc_execute <<'EOF'
int
main(void)
{
	return sizeof(char*) > sizeof(int) ? 0 : 1;
}
EOF
	then
		_sfio_defs="${_sfio_defs}#define _more_void_int	1	/* voidptr is larger than int */
"
	fi

	# more long_int — long is larger than int
	if _mc_execute <<'EOF'
int
main(void)
{
	return sizeof(long) > sizeof(int) ? 0 : 1;
}
EOF
	then
		_sfio_defs="${_sfio_defs}#define _more_long_int	1	/* long is larger than int */
"
	fi

	CFLAGS_BASE="$_saved_CFLAGS_BASE"

	{
		echo "/* : : generated by configure.sh probe_ast_sfio : : */"
		echo "#ifndef _def_sfio_ast"
		echo "#define _def_sfio_ast	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s' "$_sfio_defs"
		echo "#endif"
	} | atomic_write "$_out" || true
}

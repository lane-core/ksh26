# probe: ast-wchar — wide character support detection
# Tier 2. Tests wchar.h, wctype.h, wide char library functions,
# mbstate_t type, native wchar.h path, and emits the full AST
# wchar compatibility layer with function redirects and fallback externs.

probe_ast_wchar()
{
	_out="$1"

	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/wchar" ]; then
		return 0
	fi

	_wc_inc="-I$FEATDIR/libast -I$LIBAST_SRC -I$LIBAST_SRC/comp -I$LIBAST_SRC/include"
	_saved_CFLAGS_BASE="$CFLAGS_BASE"
	CFLAGS_BASE="$CFLAGS_BASE $_wc_inc"

	# ── All probes (before emission) ──

	# hdr probes for lib directives (stdlib.h, stdio.h, wchar.h)
	_hdr_stdlib_w=0; _mc_hdr "stdlib.h" && _hdr_stdlib_w=1
	_hdr_stdio_w=0; _mc_hdr "stdio.h" && _hdr_stdio_w=1
	_hdr_wchar_w=0; _mc_hdr "wchar.h" && _hdr_wchar_w=1

	# lib probes (mbstowcs ... wcwidth, towlower, towupper)
	_wc_lib_defs=""
	for _fn in mbstowcs wctomb wcrtomb wcslen wcstombs wcwidth \
		   towlower towupper; do
		if _mc_lib "$_fn"; then
			_wc_lib_defs="${_wc_lib_defs}#define _lib_${_fn}	1	/* ${_fn}() in default lib(s) */
"
		fi
	done

	# Implicit headers from typ test: time.h, sys/time.h, sys/times.h, stddef.h
	_wc_impl=""
	if _mc_hdr "time.h"; then
		_wc_impl="${_wc_impl}#define _hdr_time	1	/* #include <time.h> ok */
"
	fi
	if _mc_sys "time"; then
		_wc_impl="${_wc_impl}#define _sys_time	1	/* #include <sys/time.h> ok */
"
	fi
	if _mc_sys "times"; then
		_wc_impl="${_wc_impl}#define _sys_times	1	/* #include <sys/times.h> ok */
"
	fi
	if _mc_hdr "stddef.h"; then
		_wc_impl="${_wc_impl}#define _hdr_stddef	1	/* #include <stddef.h> ok */
"
	fi

	# typ mbstate_t
	_typ_mbstate_w=0
	if _mc_typ mbstate_t wchar.h; then
		_typ_mbstate_w=1
		_wc_impl="${_wc_impl}#define _typ_mbstate_t	1	/* mbstate_t is a type */
"
	fi

	# nxt wchar — find native wchar.h path
	_nxt_wchar=""
	_nxt_src="${_probe_tmpdir}/mc.c"
	echo "${_PROBE_STD_INC}
#include <wchar.h>" >|"$_nxt_src"
	if "$CC" $CFLAGS_BASE -E "$_nxt_src" >|"${_probe_tmpdir}/mc.i" 2>>"$_PROBE_LOG"; then
		_nxt_path=$(sed -n 's/^#[line ]*[0-9][0-9]* *"\([^"]*\/wchar\.h\)".*/\1/p' "${_probe_tmpdir}/mc.i" | \
			grep -v "$FEATDIR" | grep -v "$LIBAST_SRC" | head -1)
		if [ -n "$_nxt_path" ]; then
			echo "${_PROBE_STD_INC}
#include <../include/wchar.h>" >|"$_nxt_src"
			if "$CC" $CFLAGS_BASE -E "$_nxt_src" >/dev/null 2>>"$_PROBE_LOG"; then
				_nxt_wchar="../include/wchar.h"
			else
				_nxt_wchar="$_nxt_path"
			fi
		fi
	fi
	rm -f "$_nxt_src" "${_probe_tmpdir}/mc.i"
	if [ -n "$_nxt_wchar" ]; then
		_wc_impl="${_wc_impl}#define _nxt_wchar <${_nxt_wchar}>	/* include path for the native <wchar.h> */
#define _nxt_wchar_str \"${_nxt_wchar}\"	/* include string for the native <wchar.h> */
"
	fi

	# hdr unistd
	_hdr_unistd_w=0; _mc_hdr "unistd.h" && _hdr_unistd_w=1

	# hdr wctype + iswalpha npt test
	_wc_wctype_inc=""
	if _mc_hdr "wctype.h"; then
		# iffe: if hdr wctype, then check npt iswalpha in wchar.h
		# If iswalpha IS prototyped in wchar.h, do NOT include wctype.h
		if _mc_compile <<'EOF'
#include <wchar.h>
static int (*_test_)(wint_t) = iswalpha;
EOF
		then
			: # iswalpha IS prototyped — no wctype include needed
		else
			_wc_wctype_inc='#include <wctype.h>	/* <wchar.h> includes <wctype.h> */'
		fi
	fi

	CFLAGS_BASE="$_saved_CFLAGS_BASE"

	# ── Emit the header ──
	# Order matches iffe output exactly (verified against reference)
	{
		echo "/* : : generated by configure.sh probe_ast_wchar : : */"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		echo "#ifndef _AST_WCHAR_H"
		echo "#define _AST_WCHAR_H	1"
		echo "#if defined(__FreeBSD__) || defined(__DragonFly__)"
		echo "#define _STDFILE_DECLARED 1"
		echo "#endif"
		echo ""
		# hdr probes for lib directives
		[ "$_hdr_stdlib_w" = 1 ] && echo "#define _hdr_stdlib	1	/* #include <stdlib.h> ok */"
		[ "$_hdr_stdio_w" = 1 ] && echo "#define _hdr_stdio	1	/* #include <stdio.h> ok */"
		[ "$_hdr_wchar_w" = 1 ] && echo "#define _hdr_wchar	1	/* #include <wchar.h> ok */"
		# lib defines
		printf '%s' "$_wc_lib_defs"
		# typ implicit headers + mbstate_t + nxt
		printf '%s' "$_wc_impl"
		# run{} block: SFSTDIO guard + native wchar.h include
		echo "#ifndef _SFSTDIO_H"
		echo "#include <ast_common.h>"
		echo "#include <stdio.h> /* NODEP */"
		echo "#endif"
		[ -n "$_nxt_wchar" ] && echo "#include <${_nxt_wchar}>	/* the native wchar.h */"
		# _hdr_unistd (comes after native include in iffe's run{} output)
		[ "$_hdr_unistd_w" = 1 ] && echo "#define _hdr_unistd	1	/* #include <unistd.h> ok */"
		# wctype include if needed
		[ -n "$_wc_wctype_inc" ] && echo "$_wc_wctype_inc"
		# Verbatim run{} block
		cat <<'WCHARRUN'

#ifndef WEOF
#define WEOF		(-1)
#endif

#undef	fgetwc
#undef	fgetws
#undef	fputwc
#undef	fputws
#undef	getwc
#undef	getwchar
#undef	getws
#undef	putwc
#undef	putwchar
#undef	ungetwc

#define fgetwc		_ast_fgetwc
#define fgetws		_ast_fgetws
#define fputwc		_ast_fputwc
#define fputws		_ast_fputws
#define fwide		_ast_fwide
#define fwprintf	_ast_fwprintf
#define fwscanf		_ast_fwscanf
#define getwc		_ast_getwc
#define getwchar	_ast_getwchar
#define getws		_ast_getws
#define putwc		_ast_putwc
#define putwchar	_ast_putwchar
#define swprintf	_ast_swprintf
#define swscanf		_ast_swscanf
#define ungetwc		_ast_ungetwc
#define vfwprintf	_ast_vfwprintf
#define vfwscanf	_ast_vfwscanf
#define vswprintf	_ast_vswprintf
#define vswscanf	_ast_vswscanf
#define vwprintf	_ast_vwprintf
#define vwscanf		_ast_vwscanf
#define wprintf		_ast_wprintf
#define wscanf		_ast_wscanf

#if !_typ_mbstate_t
#undef	_typ_mbstate_t
#define _typ_mbstate_t	1
typedef char mbstate_t;
#endif

#if !_lib_mbstowcs
extern size_t		mbstowcs(wchar_t*, const char*, size_t);
#endif
#if !_lib_wctomb
extern int		wctomb(char*, wchar_t);
#endif
#if !_lib_wcrtomb
extern size_t		wcrtomb(char*, wchar_t, mbstate_t*);
#endif
#if !_lib_wcslen
extern size_t		wcslen(const wchar_t*);
#endif
#if !_lib_wcstombs
extern size_t		wcstombs(char*, const wchar_t*, size_t);
#endif

extern int		fwprintf(FILE*, const wchar_t*, ...);
extern int		fwscanf(FILE*, const wchar_t*, ...);
extern wint_t		fgetwc(FILE*);
extern wchar_t*		fgetws(wchar_t*, int, FILE*);
extern wint_t		fputwc(wchar_t, FILE*);
extern int		fputws(const wchar_t*, FILE*);
extern int		fwide(FILE*, int);
extern wint_t		getwc(FILE*);
extern wint_t		getwchar(void);
extern wchar_t*		getws(wchar_t*);
extern wint_t		putwc(wchar_t, FILE*);
extern wint_t		putwchar(wchar_t);
extern int		swprintf(wchar_t*, size_t, const wchar_t*, ...);
extern int		swscanf(const wchar_t*, const wchar_t*, ...);
extern wint_t		ungetwc(wint_t, FILE*);
extern int		vfwprintf(FILE*, const wchar_t*, va_list);
extern int		vfwscanf(FILE*, const wchar_t*, va_list);
extern int		vwprintf(const wchar_t*, va_list);
extern int		vwscanf(const wchar_t*, va_list);
extern int		vswprintf(wchar_t*, size_t, const wchar_t*, va_list);
extern int		vswscanf(const wchar_t*, const wchar_t*, va_list);
extern int		wprintf(const wchar_t*, ...);
extern int		wscanf(const wchar_t*, ...);

WCHARRUN
		# #else block for multiple-include case
		if [ -n "$_nxt_wchar" ]; then
			echo "#else"
			echo ""
			echo "/* on some systems, <wchar.h> can be included multiple times with multiple effects */"
			echo "#include <${_nxt_wchar}>	/* the native wchar.h */"
			echo ""
		fi
		echo "#endif /* !_AST_WCHAR_H */"
	} | atomic_write "$_out" || true
}

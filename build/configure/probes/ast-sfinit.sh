# probe: ast-sfinit — external sfinit.c compile
# Tier 6. Source: src/lib/libast/features/sfinit.c — standalone C program.
# Includes FEATURE/common and FEATURE/float (from tier 5).
# Emits sf_flt_pow10[], sf_dbl_pow10[], and _Sftable initializer.

probe_ast_sfinit()
{
	_out="$1"

	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/sfinit.c" ]; then
		return 0
	fi

	# .c program probe — needs FEATURE symlink + no std/
	_sfi_work="$FEATDIR/libast/_work_sfinit"
	mkdir -p "$_sfi_work"
	[ -L "$_sfi_work/FEATURE" ] || ln -sf "$FEATDIR/libast/FEATURE" "$_sfi_work/FEATURE"
	cp "$LIBAST_SRC/features/sfinit.c" "$_sfi_work/sfinit.c"

	# No -I.../std — ast stdio.h wrapper remaps printf
	_sfi_inc="-I$FEATDIR/libast -I$LIBAST_SRC -I$LIBAST_SRC/comp -I$LIBAST_SRC/include"
	_sfi_bin="$_sfi_work/sfinit_probe"

	_sfi_content=""
	if probe_run "$CC" $CFLAGS_BASE $_sfi_inc \
		-include "$FEATDIR/libast/FEATURE/standards" -include stdio.h \
		-o "$_sfi_bin" "$_sfi_work/sfinit.c" $LDFLAGS_BASE; then
		_sfi_content=$(probe_run "$_sfi_bin") || true
	else
		echo "configure.sh: warning: sfinit.c probe failed to compile" >&2
	fi
	rm -f "$_sfi_bin"

	{
		echo "/* : : generated by configure.sh probe_ast_sfinit : : */"
		echo "#ifndef _def_sfinit_ast"
		echo "#define _def_sfinit_ast	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s\n' "$_sfi_content"
		echo "#endif"
	} | atomic_write "$_out" || true
}

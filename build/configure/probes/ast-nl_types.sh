# probe: ast-nl_types — nl_types header with native path detection
# Tier 5 (complex). Detects catopen, nl_langinfo, nl_types.h, langinfo.h,
# native header path, and emits NL_SETMAX/NL_MSGMAX/catopen wrappers.
#
# Lifted from monolith probe_ast_nl_types with API translations:
# - probe_lib → _mc_lib, probe_hdr → _mc_hdr
# - probe_nxt → _mc_nxt
# - output path from $1

probe_ast_nl_types()
{
	_out="$1"

	# Cache check
	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/nl_types" ]; then
		return 0
	fi

	_saved="$CFLAGS_BASE"
	CFLAGS_BASE="$CFLAGS_BASE -I$FEATDIR/libast -I$LIBAST_SRC -I$LIBAST_SRC/comp -I$LIBAST_SRC/include"

	_nl_defs=""

	# lib probes
	_lib_catopen=0
	if _mc_lib "catopen"; then
		_lib_catopen=1
		_nl_defs="${_nl_defs}#define _lib_catopen	1	/* catopen() in default lib(s) */
"
	fi
	if _mc_lib "nl_langinfo"; then
		_nl_defs="${_nl_defs}#define _lib_nl_langinfo	1	/* nl_langinfo() in default lib(s) */
"
	fi

	# hdr probes
	_hdr_nl_types=0
	if _mc_hdr "nl_types.h"; then
		_hdr_nl_types=1
		_nl_defs="${_nl_defs}#define _hdr_nl_types	1	/* #include <nl_types.h> ok */
"
	fi
	if _mc_hdr "langinfo.h"; then
		_nl_defs="${_nl_defs}#define _hdr_langinfo	1	/* #include <langinfo.h> ok */
"
	fi

	# nxt nl_types
	_nxt_nl_types=$(_mc_nxt "nl_types")
	if [ -n "$_nxt_nl_types" ]; then
		_nl_defs="${_nl_defs}#define _nxt_nl_types <${_nxt_nl_types}>	/* include path for the native <nl_types.h> */
#define _nxt_nl_types_str \"${_nxt_nl_types}\"	/* include string for the native <nl_types.h> */
"
	fi

	# The output{} block needs FEATURE/limits for NL_SETMAX etc.
	# Compile in the workdir that has FEATURE symlink
	_nl_work="$FEATDIR/libast/_work_nl_types"
	mkdir -p "$_nl_work"
	[ -L "$_nl_work/FEATURE" ] || ln -sf "$FEATDIR/libast/FEATURE" "$_nl_work/FEATURE"

	# Build output probe
	_nl_out_src="${_nl_work}/nl_out.c"
	cat >|"$_nl_out_src" <<NLEOF
#include <stdio.h>
#define _lib_catopen $_lib_catopen
#define _hdr_nl_types $_hdr_nl_types
#if !_lib_catopen
#undef _hdr_nl_types
#endif
#include "FEATURE/limits"
#if _hdr_nl_types
#include <nl_types.h>
#endif

int
main(void)
{
	printf("#include <limits.h>\n");
NLEOF

	if [ -n "$_nxt_nl_types" ]; then
		cat >> "$_nl_out_src" <<NLEOF2
	printf("#include <${_nxt_nl_types}>	/* the native nl_types.h */\n");
NLEOF2
	fi

	cat >> "$_nl_out_src" <<'NLEOF3'
	printf("\n");
	printf("#ifdef __HAIKU__\n");
	printf("typedef int	nl_item;  /* workaround system header bug */\n");
	printf("#endif\n");
#ifndef NL_SETMAX
	printf("#undef	NL_SETMAX\n");
	printf("#define NL_SETMAX	1023\n");
#endif
#ifndef NL_MSGMAX
	printf("#undef	NL_MSGMAX\n");
	printf("#define NL_MSGMAX	32767\n");
#endif
#ifndef NL_SETD
	printf("#undef	NL_SETD\n");
	printf("#define NL_SETD		1\n");
#endif
#ifndef NL_CAT_LOCALE
	printf("#undef	NL_CAT_LOCALE\n");
	printf("#define NL_CAT_LOCALE	1\n");
#endif
#if _lib_catopen
	printf("#undef	nl_catd\n");
	printf("#define	nl_catd		_ast_nl_catd\n");
	printf("#undef	catopen\n");
	printf("#define catopen		_ast_catopen\n");
	printf("#undef	catgets\n");
	printf("#define	catgets		_ast_catgets\n");
	printf("#undef	catclose\n");
	printf("#define catclose	_ast_catclose\n");
#endif
	printf("\n");
	printf("typedef void* nl_catd;\n");
	printf("\n");
	printf("extern nl_catd		catopen(const char*, int);\n");
	printf("extern char*		catgets(nl_catd, int, int, const char*);\n");
	printf("extern int		catclose(nl_catd);\n");
	printf("\n");
	return 0;
}
NLEOF3

	_nl_out_result=""
	_nl_inc="-I$FEATDIR/libast -I$LIBAST_SRC -I$LIBAST_SRC/comp -I$LIBAST_SRC/include"
	if "$CC" $CFLAGS_BASE $_nl_inc \
		-o "${_nl_work}/nl_out" "$_nl_out_src" $LDFLAGS_BASE \
		2>>"$_PROBE_STDERR"; then
		_nl_out_result=$("${_nl_work}/nl_out" 2>>"$_PROBE_STDERR") || true
	else
		echo "configure.sh: warning: nl_types output probe failed to compile" >&2
	fi
	rm -f "${_nl_work}/nl_out"

	CFLAGS_BASE="$_saved"

	{
		echo "/* : : generated by configure.sh probe_ast_nl_types : : */"
		echo "#ifndef _def_nl_types_ast"
		echo "#define _def_nl_types_ast	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s' "$_nl_defs"
		[ -n "$_nl_out_result" ] && printf '%s\n' "$_nl_out_result"
		echo "#endif"
	} | atomic_write "$_out" || true
}

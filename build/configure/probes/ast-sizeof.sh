# probe: ast-sizeof — type sizes (needs FEATURE/common)
# Tier 5 (complex). Compiles a .c program that uses _ast_intmax_t
# from FEATURE/common to emit sizeof defines.
#
# Lifted from monolith probe_ast_sizeof with API translations:
# - output path from $1

probe_ast_sizeof()
{
	_out="$1"

	# Cache check
	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/sizeof" ]; then
		return 0
	fi

	# needs FEATURE/common for _ast_intmax_t
	_sz_work="$FEATDIR/libast/_work_sizeof"
	mkdir -p "$_sz_work"
	[ -L "$_sz_work/FEATURE" ] || ln -sf "$FEATDIR/libast/FEATURE" "$_sz_work/FEATURE"

	_sz_inc="-I$FEATDIR/libast -I$LIBAST_SRC -I$LIBAST_SRC/comp -I$LIBAST_SRC/include"

	_sz_src="${_sz_work}/sizeof.c"
	cat >|"$_sz_src" <<'EOF'
#include "FEATURE/common"
#include <stdio.h>
int
main(void)
{
	printf("#define _ast_sizeof_char	%d\n", (int)sizeof(char));
	printf("#define _ast_sizeof_short	%d\n", (int)sizeof(short));
	printf("#define _ast_sizeof_int		%d\n", (int)sizeof(int));
	printf("#define _ast_sizeof_long	%d\n", (int)sizeof(long));
	printf("#define _ast_sizeof_intmax_t	%d\n", (int)sizeof(_ast_intmax_t));
	return 0;
}
EOF

	_sz_result=""
	if "$CC" $CFLAGS_BASE $_sz_inc \
		-o "${_sz_work}/sizeof_probe" "$_sz_src" $LDFLAGS_BASE \
		2>>"$_PROBE_STDERR"; then
		_sz_result=$("${_sz_work}/sizeof_probe" 2>>"$_PROBE_STDERR") || true
	else
		echo "configure.sh: warning: sizeof probe failed to compile" >&2
	fi
	rm -f "${_sz_work}/sizeof_probe"

	{
		echo "/* : : generated by configure.sh probe_ast_sizeof : : */"
		echo "#ifndef _def_sizeof_ast"
		echo "#define _def_sizeof_ast	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		[ -n "$_sz_result" ] && printf '%s\n' "$_sz_result"
		echo "#endif"
	} | atomic_write "$_out" || true
}

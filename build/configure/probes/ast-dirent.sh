# probe: ast-dirent — dirent header and opendir detection
# Tier 5 (complex). Detects opendir, dirent.h, sys/dir.h, native
# header path via nxt, and emits the appropriate dirent wrapper.
#
# Lifted from monolith probe_ast_dirent with API translations:
# - probe_lib → _mc_lib, probe_hdr → _mc_hdr, probe_sys → _mc_sys
# - probe_nxt → _mc_nxt
# - output path from $1

probe_ast_dirent()
{
	_out="$1"

	# Cache check
	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/dirent" ]; then
		return 0
	fi

	_saved="$CFLAGS_BASE"
	CFLAGS_BASE="$CFLAGS_BASE -I$FEATDIR/libast -I$LIBAST_SRC -I$LIBAST_SRC/comp -I$LIBAST_SRC/include"

	_dir_defs=""
	_lib_opendir=0
	if _mc_lib "opendir"; then
		_lib_opendir=1
		_dir_defs="${_dir_defs}#define _lib_opendir	1	/* opendir() in default lib(s) */
"
	fi

	_hdr_dirent=0
	if _mc_hdr "dirent.h"; then
		_hdr_dirent=1
		_dir_defs="${_dir_defs}#define _hdr_dirent	1	/* #include <dirent.h> ok */
"
	fi

	_sys_dir=0
	if _mc_sys "dir"; then
		_sys_dir=1
		_dir_defs="${_dir_defs}#define _sys_dir	1	/* #include <sys/dir.h> ok */
"
	fi

	# nxt dirent
	_nxt_dirent=$(_mc_nxt "dirent")
	if [ -n "$_nxt_dirent" ]; then
		_dir_defs="${_dir_defs}#define _nxt_dirent <${_nxt_dirent}>	/* include path for the native <dirent.h> */
#define _nxt_dirent_str \"${_nxt_dirent}\"	/* include string for the native <dirent.h> */
"
	fi

	# Emit body based on which case matches (same as iffe if/elif chain)
	_dir_body=""
	if [ "$_lib_opendir" = 0 ]; then
		_dir_body='/*
 * <dirent.h> for systems with no opendir()
 */

#ifndef _DIRENT_H
#define _DIRENT_H

typedef struct
{
	int		dd_fd;		/* file descriptor		*/
#ifdef _DIR_PRIVATE_
	_DIR_PRIVATE_
#endif
} DIR;

struct dirent
{
	long		d_fileno;	/* entry serial number		*/
	int		d_reclen;	/* entry length			*/
	int		d_namlen;	/* entry name length		*/
	char		d_name[1];	/* entry name			*/
};

#ifndef _DIR_PRIVATE_

#ifdef	rewinddir
#undef	rewinddir
#define rewinddir(p)	seekdir(p,0L)
#endif

extern DIR*		opendir(const char*);
extern void		closedir(DIR*);
extern struct dirent*	readdir(DIR*);
extern void		seekdir(DIR*, long);
extern long		telldir(DIR*);

#endif

#endif'
	elif [ -n "$_nxt_dirent" ] && [ "$_hdr_dirent" = 1 ]; then
		_dir_body="/*
 * <dirent.h> for systems with ok <dirent.h>
 */

#ifndef _DIRENT_H

#include <${_nxt_dirent}>	/* the native <dirent.h> */

#ifndef _DIRENT_H
#define _DIRENT_H
#endif

#endif"
	fi

	CFLAGS_BASE="$_saved"

	{
		echo "/* : : generated by configure.sh probe_ast_dirent : : */"
		echo "#ifndef _def_dirent_ast"
		echo "#define _def_dirent_ast	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s' "$_dir_defs"
		[ -n "$_dir_body" ] && printf '%s\n' "$_dir_body"
		echo "#endif"
	} | atomic_write "$_out" || true
}

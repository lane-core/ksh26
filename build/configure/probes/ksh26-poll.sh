# probe: ksh26-poll — poll/socket features
# Tier 7. Combines probe_ksh26_poll_header + probe_ksh26_poll supplement.
# Tests headers, libs, types, and socketpair behavior.

probe_ksh26_poll()
{
	_out="$1"
	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$KSH_SRC/features/poll" ]; then
		return 0
	fi

	_defs=""

	# hdr,sys poll,socket,netinet/in
	if _mc_hdr "poll.h"; then
		_defs="${_defs}#define _hdr_poll	1	/* #include <poll.h> ok */
"
	fi
	if _mc_hdr "netinet/in.h"; then
		_defs="${_defs}#define _hdr_netinet_in	1	/* #include <netinet/in.h> ok */
"
	fi
	if _mc_sys "poll"; then
		_defs="${_defs}#define _sys_poll	1	/* #include <sys/poll.h> ok */
"
	fi
	if _mc_sys "socket"; then
		_defs="${_defs}#define _sys_socket	1	/* #include <sys/socket.h> ok */
"
	fi

	# lib select, poll, socket
	for _fn in select poll socket; do
		if _mc_lib "$_fn"; then
			_defs="${_defs}#define _lib_${_fn}	1	/* ${_fn}() in default lib(s) */
"
		fi
	done

	# lib htons, htonl (need netinet/in.h)
	for _fn in htons htonl; do
		if _mc_lib "$_fn"; then
			_defs="${_defs}#define _lib_${_fn}	1	/* ${_fn}() in default lib(s) */
"
		fi
	done

	# lib getaddrinfo (needs netdb.h)
	if _mc_hdr "netdb.h"; then
		_defs="${_defs}#define _hdr_netdb	1	/* #include <netdb.h> ok */
"
	fi
	if _mc_lib "getaddrinfo"; then
		_defs="${_defs}#define _lib_getaddrinfo	1	/* getaddrinfo() in default lib(s) */
"
	fi

	# typ headers (iffe prepends standard headers for typ probes)
	if _mc_hdr "time.h"; then
		_defs="${_defs}#define _hdr_time	1	/* #include <time.h> ok */
"
	fi
	if _mc_sys "time"; then
		_defs="${_defs}#define _sys_time	1	/* #include <sys/time.h> ok */
"
	fi
	if _mc_sys "times"; then
		_defs="${_defs}#define _sys_times	1	/* #include <sys/times.h> ok */
"
	fi
	if _mc_hdr "stddef.h"; then
		_defs="${_defs}#define _hdr_stddef	1	/* #include <stddef.h> ok */
"
	fi
	if _mc_hdr "stdlib.h"; then
		_defs="${_defs}#define _hdr_stdlib	1	/* #include <stdlib.h> ok */
"
	fi
	if _mc_sys "select"; then
		_defs="${_defs}#define _sys_select	1	/* #include <sys/select.h> ok */
"
	fi

	# typ fd_set
	if _mc_typ "fd_set" "sys/socket.h" "sys/select.h"; then
		_defs="${_defs}#define _typ_fd_set	1	/* fd_set is a type */
"
	fi

	# typ socklen_t (iffe adds unistd.h)
	if _mc_hdr "unistd.h"; then
		_defs="${_defs}#define _hdr_unistd	1	/* #include <unistd.h> ok */
"
	fi
	if _mc_typ "socklen_t" "unistd.h" "sys/socket.h"; then
		_defs="${_defs}#define _typ_socklen_t	1	/* socklen_t is a type */
"
	fi

	# cat{} block
	_cat='#if _hdr_poll
#    include    <poll.h>
#else
#   if _sys_poll
#	include    <sys/poll.h>
#   endif /* _sys_poll */
#endif /* _hdr_poll */
#if _lib_select
#   ifndef FD_ZERO
#	define FD_ZERO(x)	(*(x)=0)
#   endif /* FD_ZERO */
#   ifndef FD_SET
#	define FD_SET(n,x)	(*(x)|=(1L<<(n)))
#   endif /* FD_SET */
#   if !_typ_fd_set
	typedef long fd_set;
#   endif /* _typ_fd_set */
#endif /* _lib_select */'

	{
		echo "/* : : generated by configure.sh probe_ksh26_poll_header : : */"
		echo "#ifndef _def_poll_ksh26"
		echo "#define _def_poll_ksh26	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s' "$_defs"
		printf '%s\n' "$_cat"
		echo ""
		echo "#endif"
	} | atomic_write "$_out" || true

	# ── Supplemental probes (appended to header) ──

	# pipe_socketpair: can recv(MSG_PEEK) read from a socketpair fd?
	if _mc_execute <<'PROBE'
#include <stdio.h>
#include <stdlib.h>
#include <unistd.h>
#include <signal.h>
#include <sys/types.h>
#include <sys/socket.h>
#include <sys/wait.h>
#ifndef SHUT_RD
#define SHUT_RD		0
#endif
#ifndef SHUT_WR
#define SHUT_WR		1
#endif
static void handler(int sig) { _exit(0); }
int main(void) {
	int sfd[2];
	char buf[256];
	pid_t pid;
	static char msg[] = "hello world\n";
	if (socketpair(AF_UNIX, SOCK_STREAM, 0, sfd) < 0 ||
	    shutdown(sfd[1], SHUT_RD) < 0 ||
	    shutdown(sfd[0], SHUT_WR) < 0)
		return 1;
	if ((pid = fork()) < 0)
		return 1;
	if (pid) {
		int n;
		close(sfd[1]);
		wait(&n);
		if (recv(sfd[0], buf, sizeof(buf), MSG_PEEK) < 0)
			return 1;
		close(sfd[0]);
		signal(SIGPIPE, handler);
		if (socketpair(AF_UNIX, SOCK_STREAM, 0, sfd) < 0 ||
		    shutdown(sfd[1], SHUT_RD) < 0 ||
		    shutdown(sfd[0], SHUT_WR) < 0)
			return 1;
		close(sfd[0]);
		write(sfd[1], msg, sizeof(msg) - 1);
		return 1;
	} else {
		close(sfd[0]);
		write(sfd[1], msg, sizeof(msg) - 1);
		return 0;
	}
}
PROBE
	then
		printf '#define _pipe_socketpair\t1\n' >> "$_out"
	fi

	# socketpair_devfd: can /dev/fd/N access socketpair fds?
	if _mc_execute <<'PROBE'
#include <unistd.h>
#include <fcntl.h>
#include <sys/types.h>
#include <sys/socket.h>
int main(void) {
	int n, sfd[2];
	close(0);
	open("/dev/null", O_RDONLY);
	if ((n = open("/dev/fd/0", O_RDONLY)) < 0) return 1;
	close(n);
	if (socketpair(AF_UNIX, SOCK_STREAM, 0, sfd) < 0 ||
	    shutdown(sfd[0], 1) < 0 || shutdown(sfd[1], 0) < 0) return 1;
	close(0);
	dup(sfd[0]);
	close(sfd[0]);
	if ((n = open("/dev/fd/0", O_RDONLY)) < 0) return 1;
	return 0;
}
PROBE
	then
		printf '#define _socketpair_devfd\t1\n' >> "$_out"
	fi

	# socketpair_shutdown_mode: does fchmod work after shutdown?
	if _mc_execute <<'PROBE'
#include <sys/types.h>
#include <sys/stat.h>
#include <sys/socket.h>
int main(void) {
	int sfd[2];
	struct stat st0, st1;
	if (socketpair(AF_UNIX, SOCK_STREAM, 0, sfd) < 0 ||
	    shutdown(sfd[0], 1) < 0 || shutdown(sfd[1], 0) < 0) return 1;
	if (fstat(sfd[0], &st0) < 0 || fstat(sfd[1], &st1) < 0) return 1;
	if ((st0.st_mode & (S_IRUSR|S_IWUSR)) == S_IRUSR &&
	    (st1.st_mode & (S_IRUSR|S_IWUSR)) == S_IWUSR) return 1;
	if (fchmod(sfd[0], S_IRUSR) < 0 || fstat(sfd[0], &st0) < 0 ||
	    (st0.st_mode & (S_IRUSR|S_IWUSR)) != S_IRUSR) return 1;
	if (fchmod(sfd[1], S_IWUSR) < 0 || fstat(sfd[1], &st1) < 0 ||
	    (st1.st_mode & (S_IRUSR|S_IWUSR)) != S_IWUSR) return 1;
	return 0;
}
PROBE
	then
		printf '#define _socketpair_shutdown_mode\t1\n' >> "$_out"
	fi
}

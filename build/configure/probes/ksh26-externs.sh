# probe: ksh26-externs — ksh extern declarations
# Tier 7. Replaces: run_iffe "externs" (the stripped version) + supplemental.
# Handles all of externs natively: hdr, mem, lib, extern, cross{}, output{}.

probe_ksh26_externs()
{
	_out="$1"

	# Guard: supplemental appends are not idempotent
	grep -q '_arg_extrabytes' "$_out" 2>/dev/null && return

	_defs=""

	# hdr nc (nc.h — rare, not on Darwin/Linux)
	if _mc_hdr "nc.h"; then
		_defs="${_defs}#define _hdr_nc	1	/* #include <nc.h> ok */
"
	fi

	# hdr math (from mem test headers)
	if _mc_hdr "math.h"; then
		_defs="${_defs}#define _hdr_math	1	/* #include <math.h> ok */
"
	fi

	# mem exception.name math.h
	if _mc_mem "exception" "name" "math.h"; then
		_defs="${_defs}#define _mem_name_exception	1	/* name is a member of struct exception */
"
	fi

	# mem _exception.name math.h
	if _mc_mem "_exception" "name" "math.h"; then
		_defs="${_defs}#define _mem_name__exception	1	/* name is a member of struct _exception */
"
	fi

	# lib setreuid, setregid
	for _fn in setreuid setregid; do
		if _mc_lib "$_fn"; then
			_defs="${_defs}#define _lib_${_fn}	1	/* ${_fn}() in default lib(s) */
"
		fi
	done

	# lib memcntl sys/mman.h — also check sys/mman.h
	if _mc_sys "mman"; then
		_defs="${_defs}#define _sys_mman	1	/* #include <sys/mman.h> ok */
"
	fi
	# memcntl itself is Solaris-only; skip

	# lib,sys pstat — HP-UX only; skip
	# lib setproctitle — FreeBSD only; check
	if _mc_lib "setproctitle"; then
		_defs="${_defs}#define _lib_setproctitle	1	/* setproctitle() in default lib(s) */
"
	fi

	# extern nice, setreuid, setregid — these check if the functions
	# need explicit extern declarations. On modern systems they're
	# declared in headers, so no extern output is generated.

	# tst cross{} SHELLMAGIC — test #! interpreter support
	_shellmagic=""
	_test_script=$(mktemp "${TMPDIR:-/tmp}/ksh26-shmagic.XXXXXX")
	printf '#!%s true\nexit 1\n' "$(command -v env)" >|"$_test_script"
	chmod 755 "$_test_script"
	if "$_test_script" 2>/dev/null; then
		_shellmagic="#define SHELLMAGIC	1
"
	fi
	rm -f "$_test_script"
	_defs="${_defs}${_shellmagic}"

	# tst output{} execve_ignores_argv0
	_execve_defs=$(_mc_output <<'EOF'
#include <string.h>
#include <unistd.h>
#include <stdio.h>
extern char **environ;
int main(int argc, char *argv[])
{
	char *orig0 = argv[0], *newenv[2], b[64];
	int i;
	sprintf(b,"_KSH_EXECVE_TEST_%d=y",(int)getpid());
	newenv[0] = b;
	newenv[1] = NULL;
	for (i = 0; environ[i]; i++)
		if (strcmp(environ[i],newenv[0])==0)
			return !(strcmp(argv[0],"TEST_OK")!=0);
	argv[0] = "TEST_OK";
	execve(orig0,argv,newenv);
	return 128;
}
EOF
)
	if [ -n "$_execve_defs" ]; then
		_defs="${_defs}${_execve_defs}
"
	fi

	# tst output{} execve_dir_enoexec
	_execvedir_defs=$(_mc_output <<'EOF'
#include <sys/stat.h>
#include <unistd.h>
#include <errno.h>
#include <stdio.h>
extern char **environ;
int main(int argc, char *argv[])
{
	char	dirname[64];
	int	e;
	sprintf(dirname,".dir.%u",(unsigned int)getpid());
	mkdir(dirname,0777);
	execve(dirname,argv,environ);
	e = errno;
	rmdir(dirname);
	return !(e == ENOEXEC);
}
EOF
)
	if [ -n "$_execvedir_defs" ]; then
		_defs="${_defs}${_execvedir_defs}
"
	fi

	{
		echo "/* : : generated by configure.sh probe_ksh26_externs : : */"
		echo "#ifndef _def_externs_ksh26"
		echo "#define _def_externs_ksh26	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s' "$_defs"
		echo "#endif"
	} | atomic_write "$_out" || true

	# NV_PID — map sizeof(pid_t) to NV integer attribute flags
	_nvpid_defs=$(_mc_output <<'PROBE'
#include <sys/types.h>
#include <stdint.h>
#include <stdio.h>
int main(void) {
	if (sizeof(pid_t) == sizeof(int16_t))
		printf("#define NV_PID\t(NV_INTEGER|NV_SHORT)\n");
	else if (sizeof(pid_t) == sizeof(int32_t))
		printf("#define NV_PID\t(NV_INTEGER)\n");
	else if (sizeof(pid_t) == 8)
		printf("#define NV_PID\t(NV_INTEGER|NV_LONG)\n");
	return 0;
}
PROBE
)
	if [ -n "$_nvpid_defs" ]; then
		printf '%s\n' "$_nvpid_defs" >> "$_out"
	fi

	# _arg_extrabytes — extra bytes per argv entry beyond strlen+1
	printf '#define _arg_extrabytes\tsizeof(char*)\n' >> "$_out"
}

# probe: ast-hack — va_listval workaround detection
# Tier 5. Tries VAL=0,1,2 to find which va_listval variant compiles.
# Needs FEATURE/common for va_listval/va_listarg macros.
#
# Lifted from monolith probe_ast_hack with API translations:
# - output path from $1

probe_ast_hack()
{
	_out="$1"

	# Cache check
	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/hack" ]; then
		return 0
	fi

	# hack probe needs FEATURE/common for va_listval etc.
	_hack_work="$FEATDIR/libast/_work_hack"
	mkdir -p "$_hack_work"
	[ -L "$_hack_work/FEATURE" ] || ln -sf "$FEATDIR/libast/FEATURE" "$_hack_work/FEATURE"

	_hack_inc="-I$FEATDIR/libast -I$LIBAST_SRC -I$LIBAST_SRC/comp -I$LIBAST_SRC/include"

	# Try VAL=0, VAL=1, VAL=2 in order
	_hack_val=""
	for _val in 0 1 2; do
		_hack_src="${_hack_work}/hack_${_val}.c"
		_hack_bin="${_hack_work}/hack_${_val}"
		cat >|"$_hack_src" <<HACKEOF
#include <stdio.h>
#define VAL $_val
#include "FEATURE/common"
#if _hdr_stdarg
#include <stdarg.h>
#else
#include <varargs.h>
#endif
void foo(int bar, ...)
{
	va_list ap;
	va_start(ap, bar);
	if (bar==1)
	{
#if VAL == 0
		va_copy(ap, va_listval(va_arg(ap, va_listarg)));
#elif VAL == 1
		va_list np;
		np = va_listval(va_arg(ap, va_listarg));
		va_copy(ap, np);
#elif VAL == 2
		va_listarg np;
		np = va_listval(va_arg(ap, va_listarg));
		va_copy(ap, np);
#endif
	}
	va_end(ap);
}
int main(void)
{
	foo(0,"one",2);
	printf("#define _need_va_listval_workaround %d\n",VAL);
	return 0;
}
HACKEOF
		if "$CC" $CFLAGS_BASE $_hack_inc -o "$_hack_bin" "$_hack_src" $LDFLAGS_BASE \
			2>>"$_PROBE_STDERR"; then
			_hack_val=$("$_hack_bin" 2>>"$_PROBE_STDERR") || true
			rm -f "$_hack_bin"
			if [ -n "$_hack_val" ]; then
				break
			fi
		fi
		rm -f "$_hack_bin"
	done

	{
		echo "/* : : generated by configure.sh probe_ast_hack : : */"
		echo "#ifndef _def_hack_ast"
		echo "#define _def_hack_ast	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		[ -n "$_hack_val" ] && printf '%s\n' "$_hack_val"
		echo "#endif"
	} | atomic_write "$_out" || true
}

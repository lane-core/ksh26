# probe: ast-wctype — wctype header with native path detection
# Tier 5 (complex). Detects native wctype.h path via nxt, emits
# iswalpha wrapper macro.
#
# Lifted from monolith probe_ast_wctype with API translations:
# - probe_nxt → _mc_nxt
# - output path from $1

probe_ast_wctype()
{
	_out="$1"

	# Cache check
	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/wctype" ]; then
		return 0
	fi

	_saved="$CFLAGS_BASE"
	CFLAGS_BASE="$CFLAGS_BASE -I$FEATDIR/libast -I$LIBAST_SRC -I$LIBAST_SRC/comp -I$LIBAST_SRC/include"

	# nxt wctype
	_nxt_wctype=$(_mc_nxt "wctype")

	CFLAGS_BASE="$_saved"

	_wct_nxt_defs=""
	if [ -n "$_nxt_wctype" ]; then
		_wct_nxt_defs="#define _nxt_wctype <${_nxt_wctype}>	/* include path for the native <wctype.h> */
#define _nxt_wctype_str \"${_nxt_wctype}\"	/* include string for the native <wctype.h> */
"
	fi

	{
		echo "/* : : generated by configure.sh probe_ast_wctype : : */"
		echo "#ifndef _def_wctype_ast"
		echo "#define _def_wctype_ast	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s' "$_wct_nxt_defs"
		# run{} content — verbatim from iffe source
		if [ -n "$_nxt_wctype" ]; then
			echo "#if _hdr_wctype && defined(_nxt_wctype)"
			echo "#include <${_nxt_wctype}>	/* the native wctype.h */"
			echo "#endif"
		fi
		echo ""
		echo "#undef	iswalpha"
		echo ""
		echo "#if AST_NOMULTIBYTE"
		echo "#define iswalpha(w)	isalpha(w)"
		echo "#else"
		echo "#define iswalpha(w)	(ast.mb.alpha?(*ast.mb.alpha)(w):isalpha(w))"
		echo "#endif /* AST_NOMULTIBYTE */"
		echo "#endif"
	} | atomic_write "$_out" || true
}

# probe: ast-param — parameter detection
# Tier 2. Delegates to param.sh which uses $cc and $tmp to compile
# test programs and generate parameter-related defines.

probe_ast_param()
{
	_out="$1"

	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/param.sh" ]; then
		return 0
	fi

	# param.sh tests include sequences with $cc and $tmp
	_param_tmp=$(mktemp -d "${TMPDIR:-/tmp}/ksh26-param.XXXXXX")
	_param_cc="$CC $CFLAGS_BASE -I$FEATDIR/libast -I$LIBAST_SRC -I$LIBAST_SRC/comp -I$LIBAST_SRC/include"

	_param_content=$(
		cc="$_param_cc" tmp="$_param_tmp/param" \
			probe_run "$SHELL" "$LIBAST_SRC/features/param.sh"
	) || true

	rm -rf "$_param_tmp"

	{
		echo "/* : : generated by configure.sh probe_ast_param : : */"
		echo "#ifndef _def_param_ast"
		echo "#define _def_param_ast	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s\n' "$_param_content"
		echo "#endif"
	} | atomic_write "$_out" || true
}

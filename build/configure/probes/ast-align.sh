# probe: ast-align — .c file probe for alignment detection
# Tier 5 (delegate). Compiles src/lib/libast/features/align.c in a
# workdir with FEATURE symlink, captures output.
#
# Lifted from monolith probe_ast_align with API translations:
# - output path from $1

probe_ast_align()
{
	_out="$1"

	# Cache check
	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/align.c" ]; then
		return 0
	fi

	# .c program probe — needs FEATURE symlink + no std/
	_al_work="$FEATDIR/libast/_work_align"
	mkdir -p "$_al_work"
	[ -L "$_al_work/FEATURE" ] || ln -sf "$FEATDIR/libast/FEATURE" "$_al_work/FEATURE"
	cp "$LIBAST_SRC/features/align.c" "$_al_work/align.c"

	# No -I.../std — ast stdio.h wrapper remaps printf
	_al_inc="-I$FEATDIR/libast -I$LIBAST_SRC -I$LIBAST_SRC/comp -I$LIBAST_SRC/include"
	_al_bin="$_al_work/align_probe"

	_al_content=""
	if probe_run "$CC" $CFLAGS_BASE $_al_inc \
		-include "$FEATDIR/libast/FEATURE/standards" -include stdio.h \
		-o "$_al_bin" "$_al_work/align.c" $LDFLAGS_BASE; then
		_al_content=$(probe_run "$_al_bin") || true
	else
		echo "configure.sh: warning: align.c probe failed to compile" >&2
	fi
	rm -f "$_al_bin"

	{
		echo "/* : : generated by configure.sh probe_ast_align : : */"
		echo "#ifndef _def_align_ast"
		echo "#define _def_align_ast	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s\n' "$_al_content"
		echo "#endif"
	} | atomic_write "$_out" || true
}

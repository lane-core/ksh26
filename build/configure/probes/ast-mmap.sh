# probe: ast-mmap — mmap capability detection
# Tier 2. Tests sys/mman.h, comprehensive mmap functionality,
# mmap performance vs read, and MAP_ANON support.

probe_ast_mmap()
{
	_out="$1"

	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/mmap" ]; then
		return 0
	fi

	_mmap_inc="-I$FEATDIR/libast -I$LIBAST_SRC -I$LIBAST_SRC/comp -I$LIBAST_SRC/include"
	_saved_CFLAGS_BASE="$CFLAGS_BASE"
	CFLAGS_BASE="$CFLAGS_BASE $_mmap_inc"

	_mmap_defs=""

	# sys mman
	if _mc_sys mman; then
		_mmap_defs="${_mmap_defs}#define _sys_mman	1	/* #include <sys/mman.h> ok */
"
	fi

	# tst lib_mmap — comprehensive mmap test (execute{})
	_has_lib_mmap=0
	if _mc_execute <<EOF
${_PROBE_STD_INC}
#include <stdio.h>
#include <unistd.h>
#include <fcntl.h>
#include <sys/types.h>
#include <sys/mman.h>
#include <sys/stat.h>
#include <sys/times.h>
#define MAPSIZE (64*1024)
#define BUFSIZE	(8*1024)
#define WRITE   (64)
#define Failed(file)	(remove(file),1)
int
main(int argc, char** argv)
{
#ifdef __HAIKU__
	return 1;
#endif
	caddr_t		mm;
	char		*t, *u, *f;
	int		i, fd, okfixed;
	char		file[1024], buf[MAPSIZE];
	struct tms	stm, etm;
	clock_t		rdtm, mmtm;
	t = file;
	if (access(f = "/tmp", 0) == 0 ||
	    access(f = "/var/tmp", 0) == 0)
	{
		while (*t = *f++)
			t++;
		*t++ = '/';
	}
	u = t;
	f = argv[0];
	while (*t = *f++)
		if (*t == '/')
			t = u;
		else if (*t != '.')
			t++;
	*t++ = '.'; *t++ = 'D'; *t = 0;
	if ((fd = open(file, O_CREAT|O_TRUNC|O_WRONLY, 0666)) < 0)
		return 1;
	for (i = 0; i < sizeof(buf); ++i)
		buf[i] = '0' + (i%10);
	for (i = 0; i < WRITE; ++i)
		if (write(fd,buf,sizeof(buf)) != sizeof(buf))
			return Failed(file);
	close(fd);
#ifndef MAP_VARIABLE
#define MAP_VARIABLE	0
#endif
	if ((fd = open(file, O_RDWR)) < 0)
		return Failed(file);
	mm = mmap(NULL, sizeof(buf), (PROT_READ|PROT_WRITE),
		  (MAP_PRIVATE|MAP_VARIABLE), fd, 0);
	if(mm == NULL || mm == (caddr_t)(-1))
		return Failed(file);
	mm = mmap(mm, sizeof(buf), (PROT_READ|PROT_WRITE),
		  (MAP_PRIVATE|MAP_FIXED), fd, 0);
	okfixed = (mm == NULL || mm == (caddr_t)(-1)) ? 0 : 1;
	munmap(mm, sizeof(buf));
	close(fd);
	if((fd = open(file,  O_RDWR)) < 0)
		return Failed(file);
	times(&stm);
	for (i = 0; i < WRITE; ++i)
		if (read(fd,buf,BUFSIZE) != BUFSIZE)
			return Failed(file);
	times(&etm);
	close(fd);
	rdtm = (etm.tms_utime-stm.tms_utime) + (etm.tms_stime-stm.tms_stime);
	if ((fd = open(file, O_RDWR)) < 0)
		return Failed(file);
	times(&stm);
	for(i = 0, mm = NULL; i < WRITE; ++i)
	{	if(okfixed)
		{	mm = (caddr_t)mmap(mm, MAPSIZE,
				(PROT_READ|PROT_WRITE),
				(MAP_PRIVATE | (mm ? MAP_FIXED : MAP_VARIABLE)),
				fd, i*MAPSIZE );
		}
		else
		{	if(mm)
				munmap(mm, MAPSIZE);
			mm = (caddr_t)mmap(NULL, MAPSIZE,
				(PROT_READ|PROT_WRITE),
				(MAP_PRIVATE|MAP_VARIABLE),
				fd, i*MAPSIZE );
		}
		if(mm == (caddr_t)(-1) || mm == NULL)
			return Failed(file);
	}
	times(&etm);
	close(fd);
	remove(file);
	mmtm = (etm.tms_utime-stm.tms_utime) + (etm.tms_stime-stm.tms_stime);
	return rdtm+60 < mmtm ? 1 : 0;
}
EOF
	then
		_has_lib_mmap=1
		_mmap_defs="${_mmap_defs}#define _lib_mmap	1	/* standard mmap interface that works */
#define _mmap_worthy	2	/* mmap performance acceptable */
"
	fi

	# tst mmap_anon — the #if !_lib_mmap/(#endif trick creates a syntax
	# error if _lib_mmap isn't set. Pass it via -D.
	_mmap_dflag=""
	[ "$_has_lib_mmap" = 1 ] && _mmap_dflag="-D_lib_mmap=1"
	if _mc_execute "$_mmap_dflag" <<EOF
${_PROBE_STD_INC}
#if !_lib_mmap
(
#endif
#include <unistd.h>
#include <fcntl.h>
#include <sys/types.h>
#include <sys/mman.h>
#if defined(MAP_ANONYMOUS) && !defined(MAP_ANON)
#define MAP_ANON	MAP_ANONYMOUS
#endif
int
main(void)
{	void	*addr;
	addr = mmap(0,1024*1024,PROT_READ|PROT_WRITE,MAP_ANON|MAP_PRIVATE,-1,0);
	return (addr && addr != (void*)(-1)) ? 0 : 1;
}
EOF
	then
		_mmap_defs="${_mmap_defs}#define _mmap_anon	1	/* use mmap MAP_ANON to get raw memory */
"
	fi

	CFLAGS_BASE="$_saved_CFLAGS_BASE"

	# cat{} block at end
	{
		echo "/* : : generated by configure.sh probe_ast_mmap : : */"
		echo "#ifndef _def_mmap_ast"
		echo "#define _def_mmap_ast	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s' "$_mmap_defs"
		cat <<'MMAPCAT'

#if _NO_MMAP
#undef	_lib_mmap
#undef	_mmap_anon
#undef	_mmap_devzero
#undef	_mmap_worthy
#endif
MMAPCAT
		echo ""
		echo "#endif"
	} | atomic_write "$_out" || true
}

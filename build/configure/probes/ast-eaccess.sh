# probe: ast-eaccess — eaccess/euidaccess detection
# Tier 2. Minimal output — neither EFF_ONLY_OK nor EX_OK is defined
# on modern macOS/Linux, and eaccess/euidaccess may or may not link.
# Just emits the guard + _sys_types.

probe_ast_eaccess()
{
	_out="$1"

	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/eaccess" ]; then
		return 0
	fi

	{
		echo "/* : : generated by configure.sh probe_ast_eaccess : : */"
		echo "#ifndef _def_eaccess_ast"
		echo "#define _def_eaccess_ast	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		echo "#endif"
	} | atomic_write "$_out" || true
}

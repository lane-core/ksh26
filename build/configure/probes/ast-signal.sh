# probe: ast-signal — external signal.c compile
# Tier 6. Source: src/lib/libast/features/signal.c — standalone C program.
# Includes FEATURE/siglist (from tier 5) and FEATURE/standards.
# Emits sig_name[] and sig_text[] arrays plus SIG_MAX.

probe_ast_signal()
{
	_out="$1"

	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/signal.c" ]; then
		return 0
	fi

	# .c program probe — needs FEATURE symlink + no std/
	_sig_work="$FEATDIR/libast/_work_signal"
	mkdir -p "$_sig_work"
	[ -L "$_sig_work/FEATURE" ] || ln -sf "$FEATDIR/libast/FEATURE" "$_sig_work/FEATURE"
	cp "$LIBAST_SRC/features/signal.c" "$_sig_work/signal.c"

	# No -I.../std — ast stdio.h wrapper remaps printf
	_sig_inc="-I$FEATDIR/libast -I$LIBAST_SRC -I$LIBAST_SRC/comp -I$LIBAST_SRC/include"
	_sig_bin="$_sig_work/signal_probe"

	_sig_content=""
	if "$CC" $CFLAGS_BASE $_sig_inc -include stdio.h \
		-o "$_sig_bin" "$_sig_work/signal.c" $LDFLAGS_BASE \
		>>"$_PROBE_LOG" 2>&1; then
		_sig_content=$("$_sig_bin" 2>>"$_PROBE_LOG") || true
	else
		echo "configure.sh: warning: signal.c probe failed to compile" >&2
	fi
	rm -f "$_sig_bin"

	{
		echo "/* : : generated by configure.sh probe_ast_signal : : */"
		echo "#ifndef _def_signal_ast"
		echo "#define _def_signal_ast	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s\n' "$_sig_content"
		echo "#endif"
	} | atomic_write "$_out" || true
}

# probe: ksh26-posix8 — POSIX Issue 8 features
# Tier 7. Replaces: run_iffe "posix8" for POSIX Issue 8 detection.
# Tests headers and lib probes for pipe2, dup3, ppoll, mkostemp,
# timer_create, timer_settime.

probe_ksh26_posix8()
{
	_out="$1"
	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$KSH_SRC/features/posix8" ]; then
		return 0
	fi

	_defs=""

	# Headers referenced by lib probes
	for _h in unistd signal poll stdlib time; do
		if _mc_hdr "${_h}.h"; then
			_defs="${_defs}#define _hdr_${_h}	1	/* #include <${_h}.h> ok */
"
		fi
	done

	# lib probes: pipe2, dup3, ppoll, mkostemp, timer_create, timer_settime
	for _fn in pipe2 dup3 ppoll mkostemp timer_create timer_settime; do
		if _mc_lib "$_fn"; then
			_defs="${_defs}#define _lib_${_fn}	1	/* ${_fn}() in default lib(s) */
"
		fi
	done

	{
		echo "/* : : generated by configure.sh probe_ksh26_posix8 : : */"
		echo "#ifndef _def_posix8_ksh26"
		echo "#define _def_posix8_ksh26	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s' "$_defs"
		echo "#endif"
	} | atomic_write "$_out" || true
}

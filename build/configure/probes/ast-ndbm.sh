# probe: ast-ndbm — ndbm database detection
# Tier 5 (complex). Tries sleepycat (db), gdbm-ndbm, gdbm/ndbm,
# then plain ndbm.h.
#
# Lifted from monolith probe_ast_ndbm with API translations:
# - probe_link → _mc_link, probe_hdr → _mc_hdr
# - output path from $1

probe_ast_ndbm()
{
	_out="$1"

	# Cache check
	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/ndbm" ]; then
		return 0
	fi

	_saved="$CFLAGS_BASE"
	CFLAGS_BASE="$CFLAGS_BASE -I$FEATDIR/libast -I$LIBAST_SRC -I$LIBAST_SRC/comp -I$LIBAST_SRC/include"

	_ndbm_defs=""
	_ndbm_body=""

	# Try sleepycat (db) first
	if _mc_link "-ldb" <<'EOF'
#define DB_DBM_HSEARCH	1
#include <db.h>
int main(void)
{
	DBM*	dbm = 0;
	dbm_close(dbm);
	return 0;
}
EOF
	then
		_ndbm_body='#ifndef DB_DBM_HSEARCH
#define DB_DBM_HSEARCH	1
#include <db.h>
#endif
#define _use_ndbm	1'
	elif _mc_hdr "gdbm-ndbm.h"; then
		_ndbm_body='#include <gdbm-ndbm.h>
#define _use_ndbm	1'
	elif _mc_hdr "gdbm/ndbm.h"; then
		_ndbm_body='#include <gdbm/ndbm.h>
#define _use_ndbm	1'
	elif _mc_hdr "ndbm.h"; then
		_ndbm_defs="${_ndbm_defs}#define _hdr_ndbm	1	/* #include <ndbm.h> ok */
"
		_ndbm_body='#include <ndbm.h>
#define _use_ndbm	1'
	fi

	CFLAGS_BASE="$_saved"

	{
		echo "/* : : generated by configure.sh probe_ast_ndbm : : */"
		echo "#ifndef _def_ndbm_ast"
		echo "#define _def_ndbm_ast	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s' "$_ndbm_defs"
		[ -n "$_ndbm_body" ] && printf '%s\n' "$_ndbm_body"
		echo ""
		echo "#endif"
	} | atomic_write "$_out" || true
}

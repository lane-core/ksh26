# probe: ast-random — random number source detection
# Tier 5 (complex). Detects sys/random.h, arc4random*, getentropy,
# getrandom, and /dev/urandom, /dev/random paths.
#
# Lifted from monolith probe_ast_random with API translations:
# - probe_sys → _mc_sys, probe_lib → _mc_lib
# - output path from $1

probe_ast_random()
{
	_out="$1"

	# Cache check
	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/random" ]; then
		return 0
	fi

	_saved="$CFLAGS_BASE"
	CFLAGS_BASE="$CFLAGS_BASE -I$FEATDIR/libast -I$LIBAST_SRC -I$LIBAST_SRC/comp -I$LIBAST_SRC/include"

	_rnd_defs=""

	# sys random
	if _mc_sys "random"; then
		_rnd_defs="${_rnd_defs}#define _sys_random	1	/* #include <sys/random.h> ok */
"
	fi

	# lib probes
	_lib_arc4random=0
	for _fn in arc4random arc4random_buf arc4random_uniform; do
		if _mc_lib "$_fn"; then
			_rnd_defs="${_rnd_defs}#define _lib_${_fn}	1	/* ${_fn}() in default lib(s) */
"
			[ "$_fn" = "arc4random" ] && _lib_arc4random=1
		fi
	done
	for _fn in getentropy getrandom; do
		if _mc_lib "$_fn"; then
			_rnd_defs="${_rnd_defs}#define _lib_${_fn}	1	/* ${_fn}() in default lib(s) */
"
		fi
	done

	# pth urandom, random
	if [ -e "/dev/urandom" ]; then
		_rnd_defs="${_rnd_defs}#define _pth_urandom	\"/dev/urandom\"	/* urandom path */
"
	fi
	if [ -e "/dev/random" ]; then
		_rnd_defs="${_rnd_defs}#define _pth_random	\"/dev/random\"	/* random path */
"
	fi

	CFLAGS_BASE="$_saved"

	{
		echo "/* : : generated by configure.sh probe_ast_random : : */"
		echo "#ifndef _def_random_ast"
		echo "#define _def_random_ast	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s' "$_rnd_defs"
		echo "#endif"
	} | atomic_write "$_out" || true
}

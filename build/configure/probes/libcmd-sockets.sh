# probe: libcmd-sockets — socket features
# Tier 7. Replaces: run_iffe "sockets" for socket features.
# Tests sys/socket.h, arpa/inet.h, netinet/in.h headers and
# getsockname, getsockopt, inet_ntoa lib probes.

probe_libcmd_sockets()
{
	_out="$1"
	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBCMD_SRC/features/sockets" ]; then
		return 0
	fi

	_defs=""

	# sys socket
	if _mc_sys "socket"; then
		_defs="${_defs}#define _sys_socket	1	/* #include <sys/socket.h> ok */
"
	fi

	# hdr arpa/inet (iffe: arpa.inet → arpa/inet.h, define: _hdr_arpa_inet)
	if _mc_hdr "arpa/inet.h"; then
		_defs="${_defs}#define _hdr_arpa_inet	1	/* #include <arpa/inet.h> ok */
"
	fi

	# hdr netinet/in (iffe: netinet.in → netinet/in.h, define: _hdr_netinet_in)
	if _mc_hdr "netinet/in.h"; then
		_defs="${_defs}#define _hdr_netinet_in	1	/* #include <netinet/in.h> ok */
"
	fi

	# lib getsockname, getsockopt, inet_ntoa
	for _fn in getsockname getsockopt inet_ntoa; do
		if _mc_lib "$_fn"; then
			_defs="${_defs}#define _lib_${_fn}	1	/* ${_fn}() in default lib(s) */
"
		fi
	done

	{
		echo "/* : : generated by configure.sh probe_libcmd_sockets : : */"
		echo "#ifndef _def_sockets_cmd"
		echo "#define _def_sockets_cmd	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s' "$_defs"
		echo "#endif"
	} | atomic_write "$_out" || true
}

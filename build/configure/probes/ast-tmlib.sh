# probe: ast-tmlib — export_dynamic + tzset bypass detection
# Tier 5 (complex). Tests whether tzset() bypasses user getenv(),
# emits tmlocaltime macro accordingly.
#
# Lifted from monolith probe_ast_tmlib with API translations:
# - probe_link → _mc_link, probe_execute → _mc_execute
# - output path from $1

probe_ast_tmlib()
{
	_out="$1"

	# Cache check
	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/tmlib" ]; then
		return 0
	fi

	_saved="$CFLAGS_BASE"
	CFLAGS_BASE="$CFLAGS_BASE -I$FEATDIR/libast -I$LIBAST_SRC -I$LIBAST_SRC/comp -I$LIBAST_SRC/include"

	# The tzset_environ test: does tzset() bypass user getenv()?
	# First check if CC.EXPORT.DYNAMIC is supported — on macOS it is (via -Wl,-export_dynamic)
	# The test tries to override getenv. If tzset ignores the override, _tzset_environ=1.
	_tzset_environ=0

	# Check if the linker supports exporting dynamic symbols
	_has_export_dynamic=0
	if _mc_link "-Wl,-export_dynamic" <<'EOF'
int main(void) { return 0; }
EOF
	then
		_has_export_dynamic=1
	elif _mc_link "-rdynamic" <<'EOF'
int main(void) { return 0; }
EOF
	then
		_has_export_dynamic=1
	fi

	if [ "$_has_export_dynamic" = 1 ]; then
		# Try the actual tzset test with export_dynamic
		_ed_flag="-Wl,-export_dynamic"
		# Check which flag works
		if ! _mc_link "-Wl,-export_dynamic" <<'EOF'
int main(void) { return 0; }
EOF
		then
			_ed_flag="-rdynamic"
		fi

		if _mc_execute "$_ed_flag" <<'EOF'
#include <time.h>
#include <string.h>
extern char**	environ;
extern char *tzname[];
char* getenv(const char* s)
{
	(void)s;
	return "foo0bar";
}
int main(void)
{
	tzset();
	return tzname[0] && !strcmp(tzname[0], "foo") &&
	       tzname[1] && !strcmp(tzname[1], "bar");
}
EOF
		then
			_tzset_environ=1
		fi
	else
		# No export dynamic: iffe's test becomes `return 0` (always succeeds),
		# so _tzset_environ=1 unconditionally — assume tzset bypasses getenv.
		_tzset_environ=1
	fi

	CFLAGS_BASE="$_saved"

	_tmlib_body=""
	if [ "$_tzset_environ" = 1 ]; then
		_tmlib_body="#define _tzset_environ	1	/* tzset() bypasses user getenv() */
#define tmlocaltime(p)		_tm_localtime(p)

extern struct tm*		_tm_localtime(const time_t*);
"
	else
		_tmlib_body="#define tmlocaltime(p)		localtime(p)
"
	fi

	{
		echo "/* : : generated by configure.sh probe_ast_tmlib : : */"
		echo "#ifndef _def_tmlib_ast"
		echo "#define _def_tmlib_ast	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s' "$_tmlib_body"
		echo "#endif"
	} | atomic_write "$_out" || true
}

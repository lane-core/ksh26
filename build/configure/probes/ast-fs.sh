# probe: ast-fs — filesystem probes
# Tier 2. Tests sync, stat members, statfs/statvfs, getfsstat,
# getmntinfo, mount headers, and emits FS_default + cat{} block.

probe_ast_fs()
{
	_out="$1"

	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/fs" ]; then
		return 0
	fi

	_fs_inc="-I$FEATDIR/libast -I$LIBAST_SRC -I$LIBAST_SRC/comp -I$LIBAST_SRC/include"
	_saved_CFLAGS_BASE="$CFLAGS_BASE"
	CFLAGS_BASE="$CFLAGS_BASE $_fs_inc"

	_fs_defs=""

	# lib sync
	if _mc_lib sync; then
		_fs_defs="${_fs_defs}#define _lib_sync	1	/* sync() in default lib(s) */
"
	fi

	# header sys/stat.h — emit both define AND include
	_has_sys_stat=0
	if _mc_sys stat; then
		_has_sys_stat=1
		_fs_defs="${_fs_defs}#include <sys/stat.h>
"
	fi

	# define FS_default "ufs"
	_fs_defs="${_fs_defs}#define FS_default	\"ufs\"
"

	# hdr stdio,mntent,mnttab
	if _mc_hdr "stdio.h"; then
		_fs_defs="${_fs_defs}#define _hdr_stdio	1	/* #include <stdio.h> ok */
"
	fi
	for _h in mntent mnttab; do
		if _mc_hdr "${_h}.h"; then
			_fs_defs="${_fs_defs}#define _hdr_${_h}	1	/* #include <${_h}.h> ok */
"
		fi
	done

	# sys mntent,mnttab
	for _s in mntent mnttab; do
		if _mc_sys "$_s"; then
			_fs_defs="${_fs_defs}#define _sys_${_s}	1	/* #include <sys/${_s}.h> ok */
"
		fi
	done
	# sys/param.h is tested as prerequisite for sys/mount.h etc
	_has_sys_param=0
	if _mc_sys param; then
		_has_sys_param=1
		_fs_defs="${_fs_defs}#define _sys_param	1	/* #include <sys/param.h> ok */
"
	fi
	_has_sys_ucred=0
	if _mc_sys ucred; then
		_has_sys_ucred=1
		_fs_defs="${_fs_defs}#define _sys_ucred	1	/* #include <sys/ucred.h> ok */
"
	fi
	_has_sys_mount=0
	if _mc_sys mount; then
		_has_sys_mount=1
		_fs_defs="${_fs_defs}#define _sys_mount	1	/* #include <sys/mount.h> ok */
"
	fi
	for _s in fs_types statfs vfs vmount; do
		if _mc_sys "$_s"; then
			_fs_defs="${_fs_defs}#define _sys_${_s}	1	/* #include <sys/${_s}.h> ok */
"
		fi
	done

	# mem stat.st_blocks,stat.st_blksize,stat.st_rdev
	for _m in st_blocks st_blksize st_rdev; do
		if _mc_mem stat "$_m" sys/types.h sys/stat.h; then
			_fs_defs="${_fs_defs}#define _mem_${_m}_stat	1	/* ${_m} is a member of struct stat */
"
		fi
	done

	# mem statfs.f_files,statfs.f_bavail — try multiple header combos
	for _m in f_files f_bavail; do
		_found=0
		for _combo in "sys/statfs.h" "sys/vfs.h" "sys/param.h sys/mount.h"; do
			_inc_lines="${_PROBE_STD_INC}
#include <sys/types.h>"
			for _hh in $_combo; do
				_inc_lines="${_inc_lines}
#include <${_hh}>"
			done
			if _mc_compile <<EOF
${_inc_lines}
static struct statfs _i;
int n = sizeof(_i.${_m});
EOF
			then
				_found=1
				break
			fi
		done
		if [ "$_found" = 1 ]; then
			_fs_defs="${_fs_defs}#define _mem_${_m}_statfs	1	/* ${_m} is a member of struct statfs */
"
		fi
	done

	# sys statvfs + mem statvfs.f_basetype,statvfs.f_frsize
	_has_sys_statvfs=0
	if _mc_sys statvfs; then
		_has_sys_statvfs=1
		_fs_defs="${_fs_defs}#define _sys_statvfs	1	/* #include <sys/statvfs.h> ok */
"
	fi
	if [ "$_has_sys_statvfs" = 1 ]; then
		if _mc_mem statvfs f_frsize sys/types.h sys/statvfs.h; then
			_fs_defs="${_fs_defs}#define _mem_f_frsize_statvfs	1	/* f_frsize is a member of struct statvfs */
"
		fi
	fi

	# lib getmntinfo,statfs
	if _mc_lib getmntinfo; then
		_fs_defs="${_fs_defs}#define _lib_getmntinfo	1	/* getmntinfo() in default lib(s) */
"
	fi
	if _mc_lib statfs; then
		_fs_defs="${_fs_defs}#define _lib_statfs	1	/* statfs() in default lib(s) */
"
	fi

	# getfsstat: test getvfsstat (NetBSD), getfsstat+statvfs, getfsstat+statfs
	# Build -D flags from earlier probe results for conditional includes
	_fs_dflag=""
	[ "$_has_sys_mount" = 1 ] && _fs_dflag="$_fs_dflag -D_sys_mount=1"
	[ "$_has_sys_param" = 1 ] && _fs_dflag="$_fs_dflag -D_sys_param=1"
	[ "$_has_sys_ucred" = 1 ] && _fs_dflag="$_fs_dflag -D_sys_ucred=1"
	_getfsstat_done=0

	# Test getfsstat with statfs (macOS)
	if [ "$_getfsstat_done" = 0 ] && _mc_compile "$_fs_dflag" <<EOF
${_PROBE_STD_INC}
#include <sys/types.h>
#if _sys_mount
#include <sys/mount.h>
#endif
#if _sys_param
#include <sys/param.h>
#endif
#if _sys_ucred
#include <sys/ucred.h>
#endif
int
gfs(struct statfs* fs)
{
	fs->f_flags = 0;
	return getfsstat(fs, sizeof(struct statfs), MNT_WAIT);
}
EOF
	then
		_getfsstat_done=1
		_fs_defs="${_fs_defs}#define _lib_getfsstat_statfs	1	/* getfsstat uses statfs */
/* getfsstat uses statfs */
#define _lib_getfsstat		1	/* getfsstat() uses statfs */
"
	fi

	# cat{} block at end
	_fs_cat=""
	if [ "$_has_sys_statvfs" = 1 ]; then
		_fs_cat='
#include <sys/statvfs.h>
#if !_mem_statvfs_f_basetype
#if _ary_f_reserved7
#define f_basetype	f_reserved7
#endif
#endif
'
	fi

	CFLAGS_BASE="$_saved_CFLAGS_BASE"

	{
		echo "/* : : generated by configure.sh probe_ast_fs : : */"
		echo "#ifndef _def_fs_ast"
		echo "#define _def_fs_ast	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		echo "#define _sys_stat	1	/* #include <sys/stat.h> ok */"
		printf '%s' "$_fs_defs"
		[ -n "$_fs_cat" ] && printf '%s' "$_fs_cat"
		echo ""
		echo "#endif"
	} | atomic_write "$_out" || true
}

# probe: ast-limits — .c file probe for limits features
# Tier 4 (delegate). Compiles src/lib/libast/features/limits.c in a
# workdir with FEATURE symlink, captures output. Depends on conf
# (for conflim.h).
#
# Lifted from monolith probe_ast_limits with API translations:
# - output path from $1

probe_ast_limits()
{
	_out="$1"

	# Cache check
	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/limits.c" ]; then
		return 0
	fi

	# limits.c uses #include "FEATURE/..." — compile from workdir with symlink
	_lim_work="$FEATDIR/libast/_work_limits"
	mkdir -p "$_lim_work"
	[ -L "$_lim_work/FEATURE" ] || ln -sf "$FEATDIR/libast/FEATURE" "$_lim_work/FEATURE"
	cp "$LIBAST_SRC/features/limits.c" "$_lim_work/limits.c"

	# No -I.../std — ast stdio.h wrapper remaps printf
	_lim_inc="-I$FEATDIR/libast -I$LIBAST_SRC -I$LIBAST_SRC/comp -I$LIBAST_SRC/include"
	_lim_bin="$_lim_work/limits_probe"

	_lim_content=""
	if "$CC" $CFLAGS_BASE $_lim_inc -include stdio.h \
		-o "$_lim_bin" "$_lim_work/limits.c" $LDFLAGS_BASE 2>/dev/null; then
		_lim_content=$("$_lim_bin" 2>/dev/null) || true
	else
		echo "configure.sh: warning: limits.c probe failed to compile" >&2
	fi
	rm -f "$_lim_bin"

	{
		echo "/* : : generated by configure.sh probe_ast_limits : : */"
		echo "#ifndef _def_limits_ast"
		echo "#define _def_limits_ast	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s\n' "$_lim_content"
		echo "#endif"
	} | atomic_write "$_out" || true
}

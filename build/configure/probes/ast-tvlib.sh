# probe: ast-tvlib — time value library features
# Tier 5 (complex). Detects time-related functions, gettimeofday variants,
# clock_gettime, utimensat.
#
# Lifted from monolith probe_ast_tvlib with API translations:
# - probe_hdr → _mc_hdr, probe_sys → _mc_sys, probe_lib → _mc_lib
# - probe_link → _mc_link, probe_execute → _mc_execute
# - probe_output → _mc_output
# - output path from $1

probe_ast_tvlib()
{
	_out="$1"

	# Cache check
	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/tvlib" ]; then
		return 0
	fi

	_tvl_inc="-I$FEATDIR/libast -I$LIBAST_SRC -I$LIBAST_SRC/comp -I$LIBAST_SRC/include"
	_saved="$CFLAGS_BASE"
	CFLAGS_BASE="$CFLAGS_BASE $_tvl_inc"

	_tvl_defs=""

	# hdr time
	if _mc_hdr "time.h"; then
		_tvl_defs="${_tvl_defs}#define _hdr_time	1	/* #include <time.h> ok */
"
	fi

	# lib probes
	_lib_settimeofday=0
	for _fn in clock_settime gettimeofday settimeofday stime utimes; do
		if _mc_lib "$_fn"; then
			_tvl_defs="${_tvl_defs}#define _lib_${_fn}	1	/* ${_fn}() in default lib(s) */
"
			case $_fn in settimeofday) _lib_settimeofday=1 ;; esac
		fi
	done
	for _fn in nanosleep usleep; do
		if _mc_lib "$_fn"; then
			_tvl_defs="${_tvl_defs}#define _lib_${_fn}	1	/* ${_fn}() in default lib(s) */
"
		fi
	done

	# sys stat
	_has_sys_stat=0
	if _mc_sys "stat"; then
		_has_sys_stat=1
		_tvl_defs="${_tvl_defs}#define _sys_stat	1	/* #include <sys/stat.h> ok */
"
	fi

	# lib utimensat link{} test
	if _mc_link <<'EOF'
#include <fcntl.h>
#include <sys/stat.h>
static struct timespec	ts[2];
int
main(void)
{
	ts[0].tv_nsec = UTIME_NOW;
	ts[1].tv_nsec = UTIME_OMIT;
	return utimensat(AT_FDCWD, ".", ts, AT_SYMLINK_NOFOLLOW) != 0;
}
EOF
	then
		_tvl_defs="${_tvl_defs}#define _lib_utimensat	1	/* complete utimensat implementation */
"
	fi

	# sys time
	if _mc_sys "time"; then
		_tvl_defs="${_tvl_defs}#define _sys_time	1	/* #include <sys/time.h> ok */
"
	fi

	# lib clock_gettime execute{}
	if _mc_execute <<'EOF'
#include <time.h>
int
main(void)
{
	struct timespec	tv;
	return clock_gettime(CLOCK_REALTIME, &tv) != 0;
}
EOF
	then
		_tvl_defs="${_tvl_defs}#define _lib_clock_gettime	1	/* execute{\ passed */
"
	fi

	# gettimeofday macro — try N=1 first (two-arg with timezone*)
	_tvl_gettimeofday=$(_mc_output "-D_lib_settimeofday=$_lib_settimeofday" <<'EOF'
#include <stdio.h>
#include <sys/types.h>
#include <sys/time.h>
int
main(void)
{
	struct timeval	tv;
	struct timezone	tz;
	if (gettimeofday(&tv, NULL) < 0)
		return 1;
	printf("#define tmgettimeofday(p)	gettimeofday(p,NULL)\n");
#if _lib_settimeofday
	printf("#define tmsettimeofday(p)	settimeofday(p,NULL)\n");
#endif
	return 0;
}
EOF
)
	if [ -z "$_tvl_gettimeofday" ]; then
		# Try N=2 variant
		_tvl_gettimeofday=$(_mc_output "-D_lib_settimeofday=$_lib_settimeofday" <<'EOF'
#include <stdio.h>
#include <sys/types.h>
#include <sys/time.h>
int
main(void)
{
	struct timeval	tv;
	if (gettimeofday(&tv, NULL) < 0)
		return 1;
	printf("#define tmgettimeofday(p)	gettimeofday(p,NULL)\n");
#if _lib_settimeofday
	printf("#define tmsettimeofday(p)	gettimeofday(p,NULL)\n");
#endif
	return 0;
}
EOF
)
	fi
	if [ -z "$_tvl_gettimeofday" ]; then
		# Try N=3 variant (one-arg)
		_tvl_gettimeofday=$(_mc_output "-D_lib_settimeofday=$_lib_settimeofday" <<'EOF'
#include <stdio.h>
#include <sys/types.h>
#include <sys/time.h>
int
main(void)
{
	struct timeval	tv;
	if (gettimeofday(&tv) < 0)
		return 1;
	printf("#define tmgettimeofday(p)	gettimeofday(p)\n");
#if _lib_settimeofday
	printf("#define tmsettimeofday(p)	settimeofday(p)\n");
#endif
	return 0;
}
EOF
)
	fi

	CFLAGS_BASE="$_saved"

	{
		echo "/* : : generated by configure.sh probe_ast_tvlib : : */"
		echo "#ifndef _def_tvlib_ast"
		echo "#define _def_tvlib_ast	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s' "$_tvl_defs"
		[ -n "$_tvl_gettimeofday" ] && printf '%s\n' "$_tvl_gettimeofday"
		echo "#endif"
	} | atomic_write "$_out" || true
}

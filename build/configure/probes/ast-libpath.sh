# probe: ast-libpath — delegates to libpath.sh
# Tier 6. Source: src/lib/libast/features/libpath.sh — standalone shell script.
# Probes for the dynamic linker and emits CONF_LIBPATH.

probe_ast_libpath()
{
	_out="$1"

	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/libpath.sh" ]; then
		return 0
	fi

	# Run the shell script. It prints a single #define line.
	_lp_define=""
	_lp_define=$(HOSTTYPE="$HOSTTYPE" "$SHELL" "$LIBAST_SRC/features/libpath.sh" 2>>"$_PROBE_STDERR") || true
	if [ -z "$_lp_define" ]; then
		# Fallback: try with sh
		_lp_define=$(HOSTTYPE="$HOSTTYPE" sh "$LIBAST_SRC/features/libpath.sh" 2>>"$_PROBE_STDERR") || true
	fi

	{
		echo "/* : : generated by configure.sh probe_ast_libpath : : */"
		echo "#ifndef _def_libpath_ast"
		echo "#define _def_libpath_ast	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		[ -n "$_lp_define" ] && printf '%s\n' "$_lp_define"
		echo "#endif"
	} | atomic_write "$_out" || true
}

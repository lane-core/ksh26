# probe: ast-syscall — syscall detection (SYS_getcwd)
# Tier 5. Tests whether syscall(SYS_getcwd, ...) works.
#
# Lifted from monolith probe_ast_syscall with API translations:
# - probe_link → _mc_link
# - output path from $1

probe_ast_syscall()
{
	_out="$1"

	# Cache check
	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/syscall" ]; then
		return 0
	fi

	_saved="$CFLAGS_BASE"
	CFLAGS_BASE="$CFLAGS_BASE -I$FEATDIR/libast -I$LIBAST_SRC -I$LIBAST_SRC/comp -I$LIBAST_SRC/include"

	_sc_defs=""

	# lib sysgetcwd link{}
	_lib_sysgetcwd=0
	if _mc_link <<'EOF'
#include <sys/syscall.h>
#include <unistd.h>
int main(void)
{
	char	buf[256];
	return syscall(SYS_getcwd, buf, sizeof(buf)) < 0;
}
EOF
	then
		_lib_sysgetcwd=1
	fi

	if [ "$_lib_sysgetcwd" = 1 ]; then
		_sc_defs="${_sc_defs}#include <sys/syscall.h>
#define SYSGETCWD(a,b)		syscall(SYS_getcwd,a,b)
"
	fi

	CFLAGS_BASE="$_saved"

	{
		echo "/* : : generated by configure.sh probe_ast_syscall : : */"
		echo "#ifndef _def_syscall_ast"
		echo "#define _def_syscall_ast	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s' "$_sc_defs"
		echo "#endif"
	} | atomic_write "$_out" || true
}

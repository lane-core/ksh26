# probe: ksh26-time — ksh time features
# Tier 7. Detects time-related features.
# Tests headers, libs, struct members, and emits timeradd/timersub macros.

probe_ksh26_time()
{
	_out="$1"
	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$KSH_SRC/features/time" ]; then
		return 0
	fi

	_defs=""

	# hdr utime
	if _mc_hdr "utime.h"; then
		_defs="${_defs}#define _hdr_utime	1	/* #include <utime.h> ok */
"
	fi

	# hdr sys/resource (iffe "hdr" for X/Y format generates _hdr_X_Y)
	if _mc_hdr "sys/resource.h"; then
		_defs="${_defs}#define _hdr_sys_resource	1	/* #include <sys/resource.h> ok */
"
	fi

	# lib getrusage, gettimeofday, setitimer
	for _fn in getrusage gettimeofday setitimer; do
		if _mc_lib "$_fn"; then
			_defs="${_defs}#define _lib_${_fn}	1	/* ${_fn}() in default lib(s) */
"
		fi
	done

	# sys/time.h (implicit from mem test)
	if _mc_sys "time"; then
		_defs="${_defs}#define _sys_time	1	/* #include <sys/time.h> ok */
"
	fi

	# mem timeval.tv_usec sys/time.h
	if _mc_mem "timeval" "tv_usec" "sys/time.h"; then
		_defs="${_defs}#define _mem_tv_usec_timeval	1	/* tv_usec is a member of struct timeval */
"
	fi

	# tst lib_2_timeofday — 2 arg gettimeofday()
	_lib_2_timeofday=0
	if _mc_link <<EOF
${_PROBE_STD_INC}
#include <sys/types.h>
#include <sys/time.h>
int main(void)
{
	struct timeval tv;
	struct timezone tz;
	return gettimeofday(&tv, &tz);
}
EOF
	then
		_lib_2_timeofday=1
		_defs="${_defs}#define _lib_2_timeofday	1	/* 2 arg gettimeofday() */
"
	fi

	# tst lib_1_timeofday — 1 arg gettimeofday()
	if [ "$_lib_2_timeofday" = 0 ]; then
		if _mc_link <<EOF
${_PROBE_STD_INC}
#include <sys/types.h>
#include <sys/time.h>
int main(void)
{
	struct timeval tv;
	return gettimeofday(&tv);
}
EOF
		then
			_defs="${_defs}#define _lib_1_timeofday	1	/* 1 arg gettimeofday() */
"
		fi
	fi

	# cat{} block — emitted verbatim
	_cat='#undef _def_time
#include	<times.h>
#include	<sys/time.h>
#define _def_time	1
#undef timeofday
#if _lib_2_timeofday
#define timeofday(p)	gettimeofday(p,NULL)
#else
#if _lib_1_timeofday
#define timeofday(p)	gettimeofday(p)
#endif
#endif
/* BSD timeradd and timersub macros */
#if !defined(timeradd)
#define	timeradd(tvp, uvp, vvp)						\
do {								\
	(vvp)->tv_sec = (tvp)->tv_sec + (uvp)->tv_sec;		\
	(vvp)->tv_usec = (tvp)->tv_usec + (uvp)->tv_usec;	\
	if ((vvp)->tv_usec >= 1000000) {			\
		(vvp)->tv_sec++;				\
		(vvp)->tv_usec -= 1000000;			\
	}							\
} while(0)
#endif
#if !defined(timersub)
#define	timersub(tvp, uvp, vvp)						\
do {								\
	(vvp)->tv_sec = (tvp)->tv_sec - (uvp)->tv_sec;		\
	(vvp)->tv_usec = (tvp)->tv_usec - (uvp)->tv_usec;	\
	if ((vvp)->tv_usec < 0) {				\
		(vvp)->tv_sec--;				\
		(vvp)->tv_usec += 1000000;			\
	}							\
} while(0)
#endif'

	{
		echo "/* : : generated by configure.sh probe_ksh26_time : : */"
		echo "#ifndef _def_time_ksh26"
		echo "#define _def_time_ksh26	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s' "$_defs"
		printf '%s\n' "$_cat"
		echo ""
		echo "#endif"
	} | atomic_write "$_out" || true
}

# probe: ast-tv — nanosecond stat timestamp detection
# Tier 5 (complex). Detects which nsec access pattern the system
# has (POSIX-1.2017 vs Darwin/BSD vs legacy) and emits the Tv_t
# type and ST_*TIME_NSEC_GET/SET macros.
#
# Lifted from monolith probe_ast_tv with API translations:
# - probe_mem → _mc_mem
# - output path from $1

probe_ast_tv()
{
	_out="$1"

	# Cache check
	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/tv" ]; then
		return 0
	fi

	_saved="$CFLAGS_BASE"
	CFLAGS_BASE="$CFLAGS_BASE -I$FEATDIR/libast -I$LIBAST_SRC -I$LIBAST_SRC/comp -I$LIBAST_SRC/include"

	# Detect which nsec access pattern the system has
	_nsec_defs=""
	if _mc_mem "stat" "st_mtim.tv_nsec" "sys/stat.h"; then
		# POSIX-1.2017 (Linux)
		_nsec_defs='#define ST_ATIME_NSEC_GET(st)	((st)->st_atim.tv_nsec)
#define ST_CTIME_NSEC_GET(st)	((st)->st_ctim.tv_nsec)
#define ST_MTIME_NSEC_GET(st)	((st)->st_mtim.tv_nsec)'
	elif _mc_mem "stat" "st_mtimespec.tv_nsec" "sys/stat.h"; then
		# Darwin/BSD
		_nsec_defs='#define ST_ATIME_NSEC_GET(st)	((st)->st_atimespec.tv_nsec)
#define ST_CTIME_NSEC_GET(st)	((st)->st_ctimespec.tv_nsec)
#define ST_MTIME_NSEC_GET(st)	((st)->st_mtimespec.tv_nsec)'
	elif _mc_mem "stat" "st_mtim.st__tim.tv_nsec" "sys/stat.h"; then
		_nsec_defs='#define ST_ATIME_NSEC_GET(st)	((st)->st_atim.st__tim.tv_nsec)
#define ST_CTIME_NSEC_GET(st)	((st)->st_ctim.st__tim.tv_nsec)
#define ST_MTIME_NSEC_GET(st)	((st)->st_mtim.st__tim.tv_nsec)'
	elif _mc_mem "stat" "st_mtimensec" "sys/stat.h"; then
		_nsec_defs='#define ST_ATIME_NSEC_GET(st)	((st)->st_atimensec)
#define ST_CTIME_NSEC_GET(st)	((st)->st_ctimensec)
#define ST_MTIME_NSEC_GET(st)	((st)->st_mtimensec)'
	else
		_nsec_defs='#define ST_ATIME_NSEC_GET(st)	0
#define ST_CTIME_NSEC_GET(st)	0
#define ST_MTIME_NSEC_GET(st)	0

#define ST_ATIME_NSEC_SET(st,n)	0
#define ST_CTIME_NSEC_SET(st,n)	0
#define ST_MTIME_NSEC_SET(st,n)	0'
	fi

	# Set macros (only if nsec is available)
	_set_defs=""
	case "$_nsec_defs" in
	*"ST_ATIME_NSEC_SET"*)
		# Already included in the else branch above (no nsec)
		;;
	*)
		_set_defs='
#define ST_ATIME_NSEC_SET(st,n)	(ST_ATIME_NSEC_GET(st)=(n))
#define ST_CTIME_NSEC_SET(st,n)	(ST_CTIME_NSEC_GET(st)=(n))
#define ST_MTIME_NSEC_SET(st,n)	(ST_MTIME_NSEC_GET(st)=(n))'
		;;
	esac

	CFLAGS_BASE="$_saved"

	{
		echo "/* : : generated by configure.sh probe_ast_tv : : */"
		echo "#ifndef _TV_H"
		echo "#define _TV_H	1"
		cat <<'TVCAT'
/*
 * AT&T Research
 *
 * high resolution Tv_t interface definitions
 */

#include <ast.h>

#define TV_NSEC_IGNORE		(1000000000L)
#define TV_TOUCH_RETAIN		((Tv_t*)1)

typedef struct Tv_s
{
	time_t		tv_sec;
	uint32_t	tv_nsec;
} Tv_t;
TVCAT
		echo ""
		printf '%s\n' "$_nsec_defs"
		[ -n "$_set_defs" ] && printf '%s\n' "$_set_defs"
		cat <<'TVCAT2'

#define tvgetatime(t,s)	((t)->tv_nsec=ST_ATIME_NSEC_GET(s),(t)->tv_sec=(s)->st_atime)
#define tvgetmtime(t,s)	((t)->tv_nsec=ST_MTIME_NSEC_GET(s),(t)->tv_sec=(s)->st_mtime)
#define tvgetctime(t,s)	((t)->tv_nsec=ST_CTIME_NSEC_GET(s),(t)->tv_sec=(s)->st_ctime)

#define tvsetatime(t,s)	(ST_ATIME_NSEC_SET(s,(t)->tv_nsec),(s)->st_atime=(t)->tv_sec)
#define tvsetmtime(t,s)	(ST_MTIME_NSEC_SET(s,(t)->tv_nsec),(s)->st_mtime=(t)->tv_sec)
#define tvsetctime(t,s)	(ST_CTIME_NSEC_SET(s,(t)->tv_nsec),(s)->st_ctime=(t)->tv_sec)

extern int		tvgettime(Tv_t*);
extern int		tvsettime(const Tv_t*);
extern int		tvcmp(const Tv_t*, const Tv_t*);
extern int		tvtouch(const char*, const Tv_t*, const Tv_t*, const Tv_t*, int);
extern int		tvsleep(const Tv_t*, Tv_t*);
TVCAT2
		echo ""
		echo "#endif"
	} | atomic_write "$_out" || true
}

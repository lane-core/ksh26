# probe: ast-locale — shell-based locale detection
# Tier 6. Source: src/lib/libast/features/locale — iffe script.
# A tst run{} block: runs locale -a to find the canonical UTF-8 name.

probe_ast_locale()
{
	_out="$1"

	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/locale" ]; then
		return 0
	fi

	# Replicate the iffe tst run{} shell block
	_loc_str=""
	_loc_sys=""
	_loc_ifs="$IFS"
	for _loc_i in $(PATH=/bin:/usr/bin:$PATH locale -a 2>/dev/null | grep -i '^[^C].*\.UTF[-8]*$'); do
		IFS=.
		set -- '' $_loc_i
		IFS="$_loc_ifs"
		case $3 in
		UTF-8)	_loc_str=$3
			break
			;;
		*)	if "$SHELL" -c "LC_CTYPE=$2.UTF-8 PATH=/bin:/usr/bin:\$PATH locale LC_CTYPE | grep -i 'utf.*8'" >/dev/null 2>&1; then
				_loc_str=UTF-8
				break
			fi
			;;
		esac
		_loc_sys=$3
	done
	case $_loc_str in
	'')	_loc_str=$_loc_sys ;;
	esac

	{
		echo "/* : : generated by configure.sh probe_ast_locale : : */"
		echo "#ifndef _def_locale_ast"
		echo "#define _def_locale_ast	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		case $_loc_str in
		'')	echo "#define _locale_utf8_str	0" ;;
		*)	echo "#define _locale_utf8_str	\"$_loc_str\"" ;;
		esac
		echo "#endif"
	} | atomic_write "$_out" || true
}

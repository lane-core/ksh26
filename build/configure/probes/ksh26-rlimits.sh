# probe: ksh26-rlimits — resource limits
# Tier 7. Replaces: run_iffe "rlimits" for resource limit features.
# Tests headers, libs, types for rlimit/ulimit support.

probe_ksh26_rlimits()
{
	_out="$1"
	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$KSH_SRC/features/rlimits" ]; then
		return 0
	fi

	_defs=""

	# hdr,sys resource,vlimit sys/time.h
	# Check sys/time.h (prerequisite)
	if _mc_sys "time"; then
		_defs="${_defs}#define _sys_time	1	/* #include <sys/time.h> ok */
"
	fi
	# hdr resource → resource.h (rare)
	# sys resource → sys/resource.h
	if _mc_sys "resource"; then
		_defs="${_defs}#define _sys_resource	1	/* #include <sys/resource.h> ok */
"
	fi
	# hdr,sys vlimit — obsolete (HP-UX), skip

	# lib getrlimit, ulimit, vlimit
	for _fn in getrlimit ulimit; do
		if _mc_lib "$_fn"; then
			_defs="${_defs}#define _lib_${_fn}	1	/* ${_fn}() in default lib(s) */
"
		fi
	done
	# vlimit — obsolete, skip

	# typ rlim_t — iffe prepends standard headers
	if _mc_hdr "time.h"; then
		_defs="${_defs}#define _hdr_time	1	/* #include <time.h> ok */
"
	fi
	if _mc_sys "times"; then
		_defs="${_defs}#define _sys_times	1	/* #include <sys/times.h> ok */
"
	fi
	if _mc_hdr "stddef.h"; then
		_defs="${_defs}#define _hdr_stddef	1	/* #include <stddef.h> ok */
"
	fi
	if _mc_hdr "stdlib.h"; then
		_defs="${_defs}#define _hdr_stdlib	1	/* #include <stdlib.h> ok */
"
	fi
	if _mc_typ "rlim_t" "sys/types.h" "sys/time.h" "sys/resource.h"; then
		_defs="${_defs}#define _typ_rlim_t	1	/* rlim_t is a type */
"
	fi

	# cat{} block
	_cat='
#if _sys_resource
#	include <sys/resource.h>
#endif
'

	{
		echo "/* : : generated by configure.sh probe_ksh26_rlimits : : */"
		echo "#ifndef _def_rlimits_ksh26"
		echo "#define _def_rlimits_ksh26	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s' "$_defs"
		printf '%s\n' "$_cat"
		echo ""
		echo "#endif"
	} | atomic_write "$_out" || true
}

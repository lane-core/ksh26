# probe: libcmd-ids — group/id features
# Tier 7. Replaces: run_iffe "ids" for group/id features.
# Tests lib probes for group enumeration and session ID functions.

probe_libcmd_ids()
{
	_out="$1"
	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBCMD_SRC/features/ids" ]; then
		return 0
	fi

	_defs=""

	# lib endgrent, getgrent, setgrent
	for _fn in endgrent getgrent setgrent; do
		if _mc_lib "$_fn"; then
			_defs="${_defs}#define _lib_${_fn}	1	/* ${_fn}() in default lib(s) */
"
		fi
	done

	# lib fsid, getfsgid, isfsg (with sys/fss.h, fsg.h, -lfsg)
	# These are OS-specific (Cray UNICOS). Not present on Darwin/Linux.

	# lib getsid unistd.h
	if _mc_hdr "unistd.h"; then
		_defs="${_defs}#define _hdr_unistd	1	/* #include <unistd.h> ok */
"
	fi
	if _mc_lib "getsid"; then
		_defs="${_defs}#define _lib_getsid	1	/* getsid() in default lib(s) */
"
	fi

	# mac fsid — check if fsid is a macro
	# (On some systems fsid is a macro in sys/fss.h)

	# cat{} block
	_cat='#if !_lib_fsid && _mac_fsid
#define _lib_fsid	1
#endif'

	{
		echo "/* : : generated by configure.sh probe_libcmd_ids : : */"
		echo "#ifndef _def_ids_cmd"
		echo "#define _def_ids_cmd	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s' "$_defs"
		printf '%s\n' "$_cat"
		echo ""
		echo "#endif"
	} | atomic_write "$_out" || true
}

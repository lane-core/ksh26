# probe: ksh26-options — ksh options
# Tier 7. Combines probe_ksh26_options_header + probe_ksh26_options supplement.
# The tst output{} block in the iffe source uses AST headers and pathicase()
# which can't compile without libast. Only sys acct is probed by iffe.
# The supplement appends SHOPT_GLOBCASEDET.

probe_ksh26_options()
{
	_out="$1"
	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$KSH_SRC/features/options" ]; then
		return 0
	fi

	_defs=""

	# sys acct
	if _mc_sys "acct"; then
		_defs="${_defs}#define _sys_acct	1	/* #include <sys/acct.h> ok */
"
	fi

	{
		echo "/* : : generated by configure.sh probe_ksh26_options_header : : */"
		echo "#ifndef _def_options_ksh26"
		echo "#define _def_options_ksh26	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s' "$_defs"
		echo "#endif"
	} | atomic_write "$_out" || true

	# ── Supplemental: SHOPT_GLOBCASEDET ──
	_globcase_defs=$(_mc_output <<'PROBE'
#include <stdio.h>
#include <unistd.h>
int main(void) {
	long r;
#ifdef _PC_CASE_INSENSITIVE
	r = pathconf("/", _PC_CASE_INSENSITIVE);
#elif defined(_PC_CASE_SENSITIVE)
	r = pathconf("/", _PC_CASE_SENSITIVE);
	if (r >= 0) r = !r;
#else
	r = -1;
#endif
	if (r > -1)
		printf("#ifndef SHOPT_GLOBCASEDET\n#   define SHOPT_GLOBCASEDET\t1\n#endif\n");
	return 0;
}
PROBE
)
	if [ -n "$_globcase_defs" ]; then
		printf '%s\n' "$_globcase_defs" >> "$_out"
	fi
}

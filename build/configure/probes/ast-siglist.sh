# probe: ast-siglist — signal list generation
# Tier 5 (complex). Detects strsignal, then uses kill -l to enumerate
# signals and emit SIG* conditional defines.
#
# Lifted from monolith probe_ast_siglist with API translations:
# - probe_hdr → _mc_hdr, probe_lib → _mc_lib
# - output path from $1

probe_ast_siglist()
{
	_out="$1"

	# Cache check
	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/siglist" ]; then
		return 0
	fi

	_saved="$CFLAGS_BASE"
	CFLAGS_BASE="$CFLAGS_BASE -I$FEATDIR/libast -I$LIBAST_SRC -I$LIBAST_SRC/comp -I$LIBAST_SRC/include"

	_sl_defs=""

	# lib strsignal
	if _mc_hdr "string.h"; then
		_sl_defs="${_sl_defs}#define _hdr_string	1	/* #include <string.h> ok */
"
	fi
	if _mc_hdr "signal.h"; then
		_sl_defs="${_sl_defs}#define _hdr_signal	1	/* #include <signal.h> ok */
"
	fi
	if _mc_lib "strsignal"; then
		_sl_defs="${_sl_defs}#define _lib_strsignal	1	/* strsignal() in default lib(s) */
"
	fi

	CFLAGS_BASE="$_saved"

	# The run{} block: get signal list from kill -l
	# Use $SHELL (not bash -- may not exist in sandbox). Iterate words
	# directly and filter non-name tokens, matching iffe's approach.
	_sl_sigs=""
	_sl_raw=$("$SHELL" -c 'kill -l' 2>/dev/null || sh -c 'kill -l' 2>/dev/null || true)
	if [ -n "$_sl_raw" ]; then
		for _sig in $_sl_raw; do
			# Strip leading number+paren (e.g. "1)" or "1")
			_sig="${_sig#[0-9]*\)}"
			_sig="${_sig#[0-9]*}"
			# Strip SIG prefix if present
			case $_sig in SIG*) _sig="${_sig#SIG}" ;; esac
			# Skip empty, non-alpha, MIN/MAX
			case $_sig in
			""|*[!A-Za-z0-9_]*|*MIN|*MAX) continue ;;
			esac
			_sl_sigs="${_sl_sigs}#if defined(SIG${_sig}) && !defined(HAD_SIG${_sig})
0,\"${_sig}\",SIG${_sig},
#endif
"
		done
	fi

	{
		echo "/* : : generated by configure.sh probe_ast_siglist : : */"
		echo "#ifndef _def_siglist_ast"
		echo "#define _def_siglist_ast	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s' "$_sl_defs"
		printf '%s' "$_sl_sigs"
		echo "#endif"
	} | atomic_write "$_out" || true
}

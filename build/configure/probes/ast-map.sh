# probe: ast-map — function remapping
# Tier 2. Delegates to map.c — a C program that printf's #define
# lines for missing/remapped functions. Compiled and executed with
# a preamble prepended (map.c uses printf without including stdio.h).

probe_ast_map()
{
	_out="$1"

	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/map.c" ]; then
		return 0
	fi

	# map.c is a C program that printf's the header content.
	# It uses #if _lib_strmode, #if _std_strtol etc from FEATURE/lib.
	# iffe prepends $std/$usr/$inc (standards + accumulated headers) before
	# the file content. We replicate this with an explicit preamble since
	# map.c uses printf without including <stdio.h>.
	_map_inc="-I$FEATDIR/libast -I$LIBAST_SRC -I$LIBAST_SRC/comp -I$LIBAST_SRC/include"
	_map_preamble="${_PROBE_STD_INC}
#include <stdio.h>
#include <sys/types.h>
"
	_map_content=$({
		echo "$_map_preamble"
		cat "$LIBAST_SRC/features/map.c"
	} | _mc_output "$_map_inc") || true

	{
		echo "/* : : generated by configure.sh probe_ast_map : : */"
		echo "#ifndef _def_map_ast"
		echo "#define _def_map_ast	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		[ -n "$_map_content" ] && echo "$_map_content"
		echo "#endif"
	} | atomic_write "$_out" || true
}

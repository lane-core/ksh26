# probe: ast-asometh — fcntl locking and semaphore detection
# Tier 2. Tests fcntl(F_SETLCK[W]) and semget/semop/semctl.

probe_ast_asometh()
{
	_out="$1"

	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/asometh" ]; then
		return 0
	fi

	_asm_defs=""

	# Test fcntl(F_SETLCK[W])
	if _mc_link <<'EOF'
#include <sys/types.h>
#include <unistd.h>
#include <fcntl.h>
int main(void)
{
	struct flock	lock;
	lock.l_type = F_WRLCK;
	lock.l_whence = SEEK_SET;
	lock.l_start = 0;
	lock.l_len = 1;
	return fcntl(1, F_SETLKW, &lock) < 0;
}
EOF
	then
		_asm_defs="${_asm_defs}#define _aso_fcntl	1	/* fcntl(F_SETLCK[W]) work */
"
	fi

	# Test semaphore operations
	if _mc_link <<'EOF'
#include <sys/types.h>
#include <sys/stat.h>
#include <unistd.h>
#include <fcntl.h>
#include <sys/ipc.h>
#include <sys/sem.h>
int main(void)
{
	int		id;
	struct sembuf	sem;
	if ((id = semget(IPC_PRIVATE, 16, IPC_CREAT|IPC_EXCL|S_IRUSR|S_IWUSR)) < 0)
		return 1;
	sem.sem_num = 0;
	sem.sem_op = 1;
	sem.sem_flg = 0;
	if (semop(id, &sem, 1) < 0)
		return 1;
	if (semctl(id, 0, IPC_RMID) < 0)
		return 1;
	return 0;
}
EOF
	then
		_asm_defs="${_asm_defs}#define _aso_semaphore	1	/* semget semop semctl work */
"
	fi

	{
		echo "/* : : generated by configure.sh probe_ast_asometh : : */"
		echo "#ifndef _def_asometh_ast"
		echo "#define _def_asometh_ast	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s' "$_asm_defs"
		echo "#endif"
	} | atomic_write "$_out" || true
}

# probe: ksh26-locale — ksh locale features
# Tier 7. Detects locale/wchar features.
# Tests headers, libs, and wctrans/towctrans functionality.

probe_ksh26_locale()
{
	_out="$1"
	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$KSH_SRC/features/locale" ]; then
		return 0
	fi

	_defs=""

	# hdr locale, wchar, wctype
	for _h in locale wchar wctype; do
		if _mc_hdr "${_h}.h"; then
			_defs="${_defs}#define _hdr_${_h}	1	/* #include <${_h}.h> ok */
"
		fi
	done

	# lib localeconv, wctype, iswctype, iswblank
	# (iffe source has "lib locale,localeconv,..." but locale() is not
	# a real function; only localeconv matters)
	for _fn in localeconv wctype iswctype iswblank; do
		if _mc_lib "$_fn"; then
			_defs="${_defs}#define _lib_${_fn}	1	/* ${_fn}() in default lib(s) */
"
		fi
	done

	# tst output{} — wctrans/towctrans functional test
	_wctrans_defs=$(_mc_output <<'EOF'
#include <stdio.h>
#include <wchar.h>
#include <wctype.h>
int main(void)
{
	wctrans_t toupper = wctrans("toupper"), tolower = wctrans("tolower");
	int r = towctrans('q',toupper) == 'Q' && towctrans('Q',tolower) == 'q';
	printf("#define _lib_wctrans\t%d\n",r);
	printf("#define _lib_towctrans\t%d\n",r);
	printf("#define _typ_wctrans_t\t%d\n",r);
	return !r;
}
EOF
)
	if [ -n "$_wctrans_defs" ]; then
		_defs="${_defs}${_wctrans_defs}
"
	fi

	{
		echo "/* : : generated by configure.sh probe_ksh26_locale_header : : */"
		echo "#ifndef _def_locale_ksh26"
		echo "#define _def_locale_ksh26	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s' "$_defs"
		echo "#endif"
	} | atomic_write "$_out" || true
}

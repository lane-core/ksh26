# probe: ksh26-fchdir — fchdir features
# Tier 7. Combines probe_ksh26_fchdir_header + probe_ksh26_fchdir supplement.
# The iffe source has only tst output{} blocks that use AST headers (<ast.h>).
# iffe can't compile them without libast, so the header part is just the guard.
# The supplement probes for O_SEARCH/O_PATH + fchdir compatibility.

probe_ksh26_fchdir()
{
	_out="$1"
	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$KSH_SRC/features/fchdir" ]; then
		return 0
	fi

	{
		echo "/* : : generated by configure.sh probe_ksh26_fchdir_header : : */"
		echo "#ifndef _def_fchdir_ksh26"
		echo "#define _def_fchdir_ksh26	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		echo "#endif"
	} | atomic_write "$_out" || true

	# ── Supplemental: fchdir O_SEARCH compatibility ──
	_fchdir_defs=$(_mc_output <<'PROBE'
#include <stdio.h>
#include <fcntl.h>
#include <unistd.h>
#include <sys/stat.h>
#include <sys/wait.h>
int main(void) {
	int flags = O_DIRECTORY | O_NONBLOCK;
	char dir[64];
	pid_t child;
	int n;
#ifdef O_SEARCH
	flags |= O_SEARCH;
#elif defined(O_PATH)
	flags |= O_PATH;
#else
	return 1;
#endif
#ifdef O_CLOEXEC
	flags |= O_CLOEXEC;
#endif
	sprintf(dir, ".probe_fchdir.%u", (unsigned)getpid());
	mkdir(dir, 0777);
	child = fork();
	if (child == 0) {
		n = open(dir, flags);
		if (n < 0) return 1;
		if (fchdir(n) < 0) return 1;
		return 0;
	} else if (child == -1) {
		rmdir(dir);
		return 1;
	}
	waitpid(child, &n, 0);
	rmdir(dir);
	if (WIFEXITED(n) && WEXITSTATUS(n) == 0)
		printf("#define _fchdir_osearch_compat\t1\n");
	return 0;
}
PROBE
)
	if [ -n "$_fchdir_defs" ]; then
		printf '%s\n' "$_fchdir_defs" >> "$_out"
	fi
}

# probe: libcmd-utsname — hostname/uname features
# Tier 7. Replaces: run_iffe "utsname" for hostname/uname features.
# Tests lib probes, sys headers, and detects default OS name via uname -o.

probe_libcmd_utsname()
{
	_out="$1"
	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBCMD_SRC/features/utsname" ]; then
		return 0
	fi

	_defs=""

	# lib getdomainname, gethostid, gethostname, sethostname,
	#     syscall, systeminfo, uname, syssgi
	for _fn in getdomainname gethostid gethostname sethostname \
		   syscall uname; do
		if _mc_lib "$_fn"; then
			_defs="${_defs}#define _lib_${_fn}	1	/* ${_fn}() in default lib(s) */
"
		fi
	done
	# systeminfo, syssgi — Solaris/IRIX only; skip on Darwin/Linux

	# mem utsname.base_rel, utsname.idnumber, utsname.m_type,
	#     utsname.nodeext (sys/utsname.h) — all OS-specific, unlikely

	# sys utsname, syscall, systeminfo, syssgi
	if _mc_sys "utsname"; then
		_defs="${_defs}#define _sys_utsname	1	/* #include <sys/utsname.h> ok */
"
	fi
	if _mc_sys "syscall"; then
		_defs="${_defs}#define _sys_syscall	1	/* #include <sys/syscall.h> ok */
"
	fi

	# tst cross{} — determine default OS name via uname -o
	_os_name=""
	_uname_path=$(getconf PATH 2>/dev/null) || _uname_path=""
	if [ -n "$_uname_path" ]; then
		_os_name=$(PATH="$_uname_path" uname -o 2>/dev/null) || true
	else
		_os_name=$(uname -o 2>/dev/null) || true
	fi
	if [ -n "$_os_name" ]; then
		_defs="${_defs}#define _UNAME_os_DEFAULT     \"${_os_name}\"  /* default os name */
"
	fi

	{
		echo "/* : : generated by configure.sh probe_libcmd_utsname : : */"
		echo "#ifndef _def_utsname_cmd"
		echo "#define _def_utsname_cmd	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s' "$_defs"
		echo "#endif"
	} | atomic_write "$_out" || true
}

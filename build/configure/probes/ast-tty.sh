# probe: ast-tty — terminal capability detection
# Tier 2. Tests termios.h, tcgetattr, tcgetpgrp, _POSIX_VDISABLE,
# and emits verbatim cat{} block for terminal compatibility macros.

probe_ast_tty()
{
	_out="$1"

	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/tty" ]; then
		return 0
	fi

	_tty_inc="-I$FEATDIR/libast -I$LIBAST_SRC -I$LIBAST_SRC/comp -I$LIBAST_SRC/include"
	_saved_CFLAGS_BASE="$CFLAGS_BASE"
	CFLAGS_BASE="$CFLAGS_BASE $_tty_inc"

	_tty_defs=""

	# hdr termios (required — fail if missing)
	if _mc_hdr "termios.h"; then
		_tty_defs="${_tty_defs}#define _hdr_termios	1	/* #include <termios.h> ok */
"
	else
		echo "configure.sh: POSIX termios.h is required" >&2
		exit 1
	fi

	# lib tcgetattr termios.h (required)
	if _mc_lib tcgetattr; then
		_tty_defs="${_tty_defs}#define _lib_tcgetattr	1	/* tcgetattr() in default lib(s) */
"
	else
		echo "configure.sh: POSIX tcgetattr(3) is required" >&2
		exit 1
	fi

	# hdr unistd
	if _mc_hdr "unistd.h"; then
		_tty_defs="${_tty_defs}#define _hdr_unistd	1	/* #include <unistd.h> ok */
"
	fi

	# lib tcgetpgrp unistd.h (required)
	if _mc_lib tcgetpgrp; then
		_tty_defs="${_tty_defs}#define _lib_tcgetpgrp	1	/* tcgetpgrp() in default lib(s) */
"
	else
		echo "configure.sh: POSIX tcgetpgrp(3) is required" >&2
		exit 1
	fi

	# mac _POSIX_VDISABLE termios.h
	if _mc_compile <<'EOF'
#include <termios.h>
#ifndef _POSIX_VDISABLE
#error _POSIX_VDISABLE not defined
#endif
int x = _POSIX_VDISABLE;
EOF
	then
		_tty_defs="${_tty_defs}#define _mac__POSIX_VDISABLE	1	/* _POSIX_VDISABLE is a macro */
"
	fi

	CFLAGS_BASE="$_saved_CFLAGS_BASE"

	# cat{} block (verbatim from tty probe source)
	{
		echo "/* : : generated by configure.sh probe_ast_tty : : */"
		echo "#ifndef _def_tty_ast"
		echo "#define _def_tty_ast	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s' "$_tty_defs"
		cat <<'TTYCAT'

#if _mac__POSIX_VDISABLE
#   undef _POSIX_VDISABLE
#endif
#include <termios.h>
#include <ast_lib.h>

#undef tcgetattr
#undef tcsetattr
#undef tcgetpgrp
#undef tcsetpgrp
#undef cfgetospeed
#ifndef TCSANOW
#   define TCSANOW	TCSETS
#   define TCSADRAIN	TCSETSW
#   define TCSAFLUSH	TCSETSF
#endif /* TCSANOW */
/* The following corrects bugs in some implementations */
#if defined(TCSADFLUSH) && !defined(TCSAFLUSH)
#   define TCSAFLUSH	TCSADFLUSH
#endif /* TCSADFLUSH */
#undef TIOCGETC

#if _sys_ioctl
#   include <sys/ioctl.h>
#endif

/* set ECHOCTL if driver can echo control characters as ^c */
#ifdef LCTLECH
#   ifndef ECHOCTL
#	define ECHOCTL	LCTLECH
#   endif /* !ECHOCTL */
#endif /* LCTLECH */
#ifdef LNEW_CTLECH
#   ifndef ECHOCTL
#	define ECHOCTL  LNEW_CTLECH
#   endif /* !ECHOCTL */
#endif /* LNEW_CTLECH */
#ifdef LNEW_PENDIN
#   ifndef PENDIN
#	define PENDIN LNEW_PENDIN
#  endif /* !PENDIN */
#endif /* LNEW_PENDIN */

TTYCAT
		echo ""
		echo "#endif"
	} | atomic_write "$_out" || true
}

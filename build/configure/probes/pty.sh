# probe: pty — pty features
# Tier 7. Detects pty features, clone device, and first-pty path.
# Tests headers, libs, extern declarations, and probes for pty clone/first paths.

probe_pty()
{
	_out="$1"
	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$PTY_SRC/features/pty" ]; then
		return 0
	fi

	_defs=""
	_includes=""

	# header sys/types.h — "header" emits both #define AND #include
	# (sys/types.h is always available)
	_includes="${_includes}#include <sys/types.h>
"

	# iffe "header" directive: emit #include (not #define) for found headers.
	for _hdr in pty.h libutil.h sys/pty.h sys/ptyio.h sys/vty.h \
		    sys/ioctl.h stropts.h; do
		if _mc_header_strict "$_hdr"; then
			_includes="${_includes}#include <${_hdr}>
"
		fi
	done

	# lib openpty, _getpty, ptsname -lutil
	# Try with -lutil first, then without
	_lutil=""
	if _mc_link "-lutil" <<'EOF'
int main(void) { return 0; }
EOF
	then
		_lutil="-lutil"
		_defs="${_defs}#define _LIB_util	1	/* -lutil is a library */
"
	fi

	_lib_ptsname=0
	for _fn in openpty _getpty ptsname; do
		if _mc_lib "$_fn" "$_lutil"; then
			_defs="${_defs}#define _lib_${_fn}	1	/* ${_fn}() in default lib(s) */
"
			case $_fn in ptsname) _lib_ptsname=1 ;; esac
		fi
	done

	# lib grantpt, unlockpt, posix_openpt (stdlib.h)
	if _mc_hdr "stdlib.h"; then
		_defs="${_defs}#define _hdr_stdlib	1	/* #include <stdlib.h> ok */
"
	fi
	for _fn in grantpt unlockpt posix_openpt; do
		if _mc_lib "$_fn"; then
			_defs="${_defs}#define _lib_${_fn}	1	/* ${_fn}() in default lib(s) */
"
		fi
	done

	# lib cfmakeraw (termios.h)
	if _mc_hdr "termios.h"; then
		_defs="${_defs}#define _hdr_termios	1	/* #include <termios.h> ok */
"
	fi
	if _mc_lib "cfmakeraw"; then
		_defs="${_defs}#define _lib_cfmakeraw	1	/* cfmakeraw() in default lib(s) */
"
	fi

	# extern declarations — only emit if the function is not declared
	# in any of the included headers (iffe "extern" directive behavior)
	_externs=""
	# Build a preamble from all included headers
	_pty_preamble="${_PROBE_STD_INC}
${_includes}"
	# _getpty: IRIX-only, never in standard headers
	_externs="${_externs}extern char*	_getpty(int*, int, mode_t, int);
"
	# openpty: check if already declared (pty.h on Linux provides it)
	if ! _mc_compile <<EOF
${_pty_preamble}
void (*_p)(void) = (void(*)(void))openpty;
EOF
	then
		_externs="${_externs}extern int	openpty(int*, int*, char*, struct termios*, struct winsize*);
"
	fi
	# ptsname: check if already declared
	if ! _mc_compile <<EOF
${_pty_preamble}
void (*_p)(void) = (void(*)(void))ptsname;
EOF
	then
		_externs="${_externs}extern char*	ptsname(int);
"
	fi

	# tst output{} — _pty_clone and _pty_first
	_pty_defs=$(_mc_output <<'PROBE'
#include <fcntl.h>
#include <stdlib.h>
#include <unistd.h>
#include <sys/types.h>
#include <sys/stat.h>
#include <stdio.h>
int main(void)
{
	int		i;
	struct stat	statb;
	static char*	pty[] = { "/dev/ptyp0000", "/dev/ptym/ptyp0", "/dev/ptyp0" };
	int		fd;
	static char*	ptc[] = { "/dev/ptmx", "/dev/ptc", "/dev/ptmx_bsd" };
	for (i = 0; i < sizeof(ptc) / sizeof(ptc[0]); i++)
		if((fd = open(ptc[i], 2))>=0)
		{
			if (ptsname(fd))
			{
				printf("#define _pty_clone\t\"%s\"\n", ptc[i]);
				close(fd);
				break;
			}
			close(fd);
		}
	for (i = 0;; i++)
		if(i >= (sizeof(pty) / sizeof(pty[0]) - 1) || stat(pty[i], &statb)>=0)
		{
			printf("#define _pty_first\t\"%s\"\n", pty[i]);
			break;
		}
	return 0;
}
PROBE
)

	{
		echo "/* : : generated by configure.sh probe_pty : : */"
		echo "#ifndef _def_pty_pty"
		echo "#define _def_pty_pty	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s' "$_includes"
		printf '%s' "$_defs"
		printf '%s\n' "$_externs"
		[ -n "$_pty_defs" ] && printf '%s\n' "$_pty_defs"
		echo "#endif"
	} | atomic_write "$_out" || true
}

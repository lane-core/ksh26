# probe: ast-time — time features (headers, types, includes)
# Tier 5 (complex). Detects time-related libs, headers, types,
# and emits the time header wrapper with conditional includes.
#
# Lifted from monolith probe_ast_time with API translations:
# - probe_lib → _mc_lib, probe_hdr → _mc_hdr, probe_sys → _mc_sys
# - probe_typ → _mc_typ, probe_mem → _mc_mem
# - output path from $1

probe_ast_time()
{
	_out="$1"

	# Cache check
	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/time" ]; then
		return 0
	fi

	_saved="$CFLAGS_BASE"
	CFLAGS_BASE="$CFLAGS_BASE -I$FEATDIR/libast -I$LIBAST_SRC -I$LIBAST_SRC/comp -I$LIBAST_SRC/include"

	_tm_defs=""

	# lib probes
	for _fn in getrusage nanosleep usleep; do
		if _mc_lib "$_fn"; then
			_tm_defs="${_tm_defs}#define _lib_${_fn}	1	/* ${_fn}() in default lib(s) */
"
		fi
	done

	# hdr time
	if _mc_hdr "time.h"; then
		_tm_defs="${_tm_defs}#define _hdr_time	1	/* #include <time.h> ok */
"
	fi

	# sys time, times
	_sys_time=0
	if _mc_sys "time"; then
		_sys_time=1
		_tm_defs="${_tm_defs}#define _sys_time	1	/* #include <sys/time.h> ok */
"
	fi
	_sys_times=0
	if _mc_sys "times"; then
		_sys_times=1
		_tm_defs="${_tm_defs}#define _sys_times	1	/* #include <sys/times.h> ok */
"
	fi

	# hdr stddef, stdlib
	if _mc_hdr "stddef.h"; then
		_tm_defs="${_tm_defs}#define _hdr_stddef	1	/* #include <stddef.h> ok */
"
	fi
	if _mc_hdr "stdlib.h"; then
		_tm_defs="${_tm_defs}#define _hdr_stdlib	1	/* #include <stdlib.h> ok */
"
	fi

	# typ probes
	for _t in clock_t time_t suseconds_t; do
		if _mc_typ "$_t"; then
			_tm_defs="${_tm_defs}#define _typ_${_t}	1	/* ${_t} is a type */
"
		fi
	done

	# sys resource
	_sys_resource=0
	if _mc_sys "resource"; then
		_sys_resource=1
		_tm_defs="${_tm_defs}#define _sys_resource	1	/* #include <sys/resource.h> ok */
"
	fi

	CFLAGS_BASE="$_saved"

	# Build the body: includes + cat{} block
	_tm_body=""
	if [ "$_sys_resource" = 1 ]; then
		_tm_body="${_tm_body}#include <sys/resource.h>

"
	fi
	if [ "$_sys_time" = 1 ]; then
		_tm_body="${_tm_body}#include <sys/time.h>

"
		# Linux glibc: <sys/time.h> does not include <time.h>, so
		# struct tm and nanosleep are not visible without it.
		# Darwin/BSD: <sys/time.h> transitively includes <time.h>.
		# Match iffe's: if ! mem tm.tm_sec sys/time.h -> include time.h
		if ! _mc_mem tm tm_sec sys/time.h; then
			_tm_body="${_tm_body}#include <time.h>

"
		fi
	fi
	if [ "$_sys_times" = 1 ]; then
		_tm_body="${_tm_body}#include <sys/times.h>

"
	fi
	_tm_body="${_tm_body}#if !defined(CLOCKS_PER_SEC) && defined(CLK_TCK)
#define CLOCKS_PER_SEC	CLK_TCK
#endif
"

	{
		echo "/* : : generated by configure.sh probe_ast_time : : */"
		echo "#ifndef _def_time_ast"
		echo "#define _def_time_ast	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s' "$_tm_defs"
		printf '%s\n' "$_tm_body"
		echo "#endif"
	} | atomic_write "$_out" || true
}

# probe: ast-sig — signal handler type detection
# Tier 2. Delegates to sig.sh which uses $cc and $tmp to compile
# test programs and generate signal-related defines.

probe_ast_sig()
{
	_out="$1"

	if [ "$opt_force" = 0 ] && [ -f "$_out" ] \
	   && [ "$_out" -nt "$LIBAST_SRC/features/sig.sh" ]; then
		return 0
	fi

	# sig.sh is a shell script that generates header content.
	# It needs $cc and $tmp variables, and runs the compiler
	# to determine signal handler types.
	_sig_tmp=$(mktemp -d "${TMPDIR:-/tmp}/ksh26-sig.XXXXXX")
	_sig_cc="$CC $CFLAGS_BASE"
	_sig_inc="-I$FEATDIR/libast -I$LIBAST_SRC -I$LIBAST_SRC/comp -I$LIBAST_SRC/include"

	_sig_content=$(
		cc="$_sig_cc $_sig_inc" tmp="$_sig_tmp/sig" \
			"$SHELL" "$LIBAST_SRC/features/sig.sh"
	) || true

	rm -rf "$_sig_tmp"

	{
		echo "/* : : generated by configure.sh probe_ast_sig : : */"
		echo "#ifndef _def_sig_ast"
		echo "#define _def_sig_ast	1"
		echo "#define _sys_types	1	/* #include <sys/types.h> ok */"
		printf '%s\n' "$_sig_content"
		echo "#endif"
	} | atomic_write "$_out" || true
}

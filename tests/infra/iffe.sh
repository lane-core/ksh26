test_17_features_to_FEATURE() {
	printf '%s\n' "TEST 17: features/* => FEATURE/*"

	mkdir -p features FEATURE
	printf '%s\n' 'header stdio.h' > features/stdio
	run_iffe -r -v run features/stdio
	assert_iffe "17: features/stdio → FEATURE/stdio" 0 "" \
'iffe: test: is sys/types.h a header ... yes
iffe: test: is stdio.h a header ... yes'

	# Check the FEATURE output file
	if [ -f FEATURE/stdio ]; then
		local feat_content
		feat_content=$(cat FEATURE/stdio | normalize)
		local expected='/* : : generated from features/stdio by iffe version VERSION : : */
#ifndef _REGRESS
#define _REGRESS	1
#define _sys_types	1	/* #include <sys/types.h> ok */
#include <stdio.h>
#endif'
		if [ "$feat_content" = "$expected" ]; then
			pass=$((pass + 1))
		else
			fail=$((fail + 1))
			errors="${errors}  FAIL: 17: FEATURE/stdio content mismatch\n"
			printf 'FAIL: 17: FEATURE/stdio content mismatch\n'
		fi
	else
		fail=$((fail + 1))
		errors="${errors}  FAIL: 17: FEATURE/stdio not created\n"
		printf 'FAIL: 17: FEATURE/stdio not created\n'
	fi
	rm -rf features FEATURE
}


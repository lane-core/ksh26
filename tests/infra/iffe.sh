#!/bin/sh

# iffe.sh — regression tests for the iffe feature-detection tool
#
# Ported from iffe.tst (AT&T regress format) to standalone POSIX shell.
# All 18 test groups from the original are preserved.
#
# Requirements: CC on PATH (runs inside nix develop)
# Usage: sh tests/infra/iffe.sh

set -o nounset

# ── Configuration ────────────────────────────────────────────────────

PACKAGEROOT=${PACKAGEROOT:-$(cd "$(dirname "$0")/../.." && pwd)}
IFFE=$PACKAGEROOT/src/cmd/INIT/iffe.sh
CC=${CC:-cc}

pass=0
fail=0
errors=""

# ── Temp directory ───────────────────────────────────────────────────

tmpdir=$(mktemp -d "${TMPDIR:-/tmp}/iffe-test.XXXXXX") || exit 1
trap 'rm -rf "$tmpdir"' EXIT
cd "$tmpdir" || exit 1

# ── Helpers ──────────────────────────────────────────────────────────

# Normalize iffe version strings so tests don't break on version bumps
normalize() {
	sed -e 's/iffe version [0-9][0-9-]*/iffe version VERSION/' \
	    -e 's/[	 ]*$//'
}

# Run iffe and capture output. Sets _stdout, _stderr, _exit.
run_iffe() {
	sh "$IFFE" "$@" >"$tmpdir/_stdout" 2>"$tmpdir/_stderr"
	_exit=$?
	_stdout=$(cat "$tmpdir/_stdout")
	_stderr=$(cat "$tmpdir/_stderr")
}

# Compare actual vs expected, version-normalized. Returns 0 on match.
check_match() {
	local label=$1
	local actual=$2
	local expected=$3
	local a_norm e_norm

	a_norm=$(printf '%s\n' "$actual" | normalize)
	e_norm=$(printf '%s\n' "$expected" | normalize)

	if [ "$a_norm" = "$e_norm" ]; then
		return 0
	else
		printf '  MISMATCH (%s):\n' "$label"
		printf '  expected:\n%s\n' "$e_norm" | sed 's/^/    /'
		printf '  actual:\n%s\n' "$a_norm" | sed 's/^/    /'
		return 1
	fi
}

# Assert iffe output matches expectations
# Usage: assert_iffe "description" "expected_exit" "expected_stdout" "expected_stderr"
#   Pass "" for stdout/stderr to skip that check
assert_iffe() {
	local desc=$1
	local exp_exit=${2:-0}
	local exp_stdout=${3:-}
	local exp_stderr=${4:-}
	local ok=true

	if [ "$_exit" != "$exp_exit" ]; then
		printf '  exit: expected %s, got %s\n' "$exp_exit" "$_exit"
		ok=false
	fi
	if [ -n "$exp_stdout" ]; then
		check_match "stdout" "$_stdout" "$exp_stdout" || ok=false
	fi
	if [ -n "$exp_stderr" ]; then
		check_match "stderr" "$_stderr" "$exp_stderr" || ok=false
	fi

	if $ok; then
		pass=$((pass + 1))
	else
		fail=$((fail + 1))
		errors="${errors}  FAIL: ${desc}\n"
		printf 'FAIL: %s\n' "$desc"
	fi
}

